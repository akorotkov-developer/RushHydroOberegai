 <b> 
    <br />
   4. Структура проведения этапа 
    <br />
   </b> 
  <br />
 
  <br />
 
  <br />
 4.1. Этап состоит из четырех подэтапов: 
  <br />
 
  <br />
 4.2. Первый подэтап. &laquo;Проверка готовности участников к действиям 
  <br />
 в условиях несчастного случая на производстве&raquo; 
  <br />
 
  <br />
 4.3. Второй подэтап.&laquo;Оказание первой помощи одномупострадавшему в зависимости от выбранной ситуационной задачи&raquo;.Помощь оказывают все участники команды, входя по очереди, через определенный промежуток времени (предлагаемый вариант: &laquo;помощь оказывает вся команда, очередность вхождения на территорию ЧС определяет руководитель команды). 
  <br />
 
  <br />
 4.4. Третий подэтап.&laquo;Оказание первой помощи индивидуально каждым участником в одной из ситуационной задачи несчастного случая&raquo;. 
