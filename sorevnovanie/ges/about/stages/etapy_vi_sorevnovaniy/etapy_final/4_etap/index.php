<div align="justify"><b>1. Общие положения этапа </b> 
  <br />
 
  <br />
 
  <br />
 
  <br />
 1.1. Цель этапа &mdash; проверка знаний и практических навыков оказания первой помощи пострадавшим после несчастного случая на производстве с использованием только подручных средств иммобилизации. 
  <br />
 
  <br />
 1.2. В качестве &laquo;пострадавшего&raquo; от действия электрического тока и при решении ситуационных задач привлекаются независимые сотрудники. 
  <br />
 
  <br />
 1.3. В качестве &laquo;пострадавшего&raquo; в состоянии клинической смерти используется робот-тренажер &laquo;ГОША&raquo;. 
  <br />
   
  <br />
 1.4. В качестве &laquo;пострадавших&raquo; при решении ситуационных задач привлекаются независимые сотрудники предприятий,на базе которых проводятся соревнования. 
  <br />
 
  <br />
 1.5. На всех подэтапах в качестве средств оказания первой помощи используются подручные материалы. 
  <br />
 
  <br />
 
  <br />
 
  <br />
 
  <br />
 <b> 
    <br />
   2. Требования к участникам этапа 
    <br />
   </b> 
  <br />
 
  <br />
 
  <br />
 2.1. Состав и требования к участникам команды устанавливаются в соответствии с Положением о проведении соревнований. К прохождению этапа допускается команда, прошедшая Мандатную комиссию и допущенная ею к участию в соревнованиях. 
  <br />
 
  <br />
 2.2. Участники,прибывшие для прохождения этапа соревнований, должны иметь с собой: 
  <br />
 
  <br />
 &middot; удостоверение установленного образца с отметками о проверке знаний; 
  <br />
 
  <br />
 &middot; индивидуальная инструкция по оказанию первой помощи при несчастных случаях на производстве; 
  <br />
 
  <br />
 2.3.Одежда членов команды должна быть единого образца с обязательным наличием эмблемы предприятия. 
  <br />
 
  <br />
 2.4. Участники соревнований должны знать и уметь применять навыки оказания первой помощи, предусмотренные действующей Инструкцией по оказанию первой помощи при несчастных случаях на производстве (далее &ndash; Инструкция). 
  <br />
 
  <br />
 2.5. Руководитель команды может присутствовать при прохождении командой этапа. 
  <br />
 
  <br />
 2.6. Руководитель команды не имеет права вмешиваться в ход прохождения этапа, комментировать действия команды, вступать с участниками или судьями в переговоры. При нарушении этого требования старший судья имеет право удалить руководителя команды с этапа, начислить команде штрафные баллы. 
