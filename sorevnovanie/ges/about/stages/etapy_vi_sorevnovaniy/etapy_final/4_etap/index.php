 <b>6. Порядок проведения четвертого этапа </b> 
  <br />
 
  <br />
 
  <br />
 
  <br />
 6.1. Первый подэтап. &laquo;Проверка готовности участников к действиям 
  <br />
 в условиях несчастного случая&raquo;. 
  <br />
 
  <br />
 По прибытии команды в назначенное по графику время в помещение, 
  <br />
 где будет проводиться этап, руководитель команды докладывает старшему судье о готовности команды для выполнения действий на этапе. 
  <br />
 
  <br />
 Судейская бригада проверяет: 
  <br />
 
  <br />
 &middot; наличие у каждого участника квалификационного удостоверения; 
  <br />
 
  <br />
 &middot; наличие именной инструкции по оказанию первой помощи 
  <br />
 при несчастных случаях на производстве; 
  <br />
 
  <br />
 &middot; соответствие формы и экипировки требованиям безопасности проведения работ. 
  <br />
 
  <br />
 По окончании проверки старший судья проводит инструктаж 
  <br />
 по порядку проведения остальных подэтапов. 
  <br />
 
  <br />
 6.2.Второй подэтап. &laquo;Оказание первой помощи одному пострадавшему в зависимости от выбранной ситуационной задачи (игры)&raquo;. Помощь оказывает каждый участник команды, входя по очереди. Участвует группа из четырех человек. Продолжительность &ndash; 5 минут. - Началом отсчета времени работы бригады считать момент подачи команды: СТАРТ. 
  <br />
 
  <br />
 Участники по одному заходят на площадку и оказывают помощь пострадавшему в зависимости от выбранной ситуационной задачи; 
  <br />
 
  <br />
 После завершения оказания помощи, необходимо эвакуировать пострадавшего за финишную черту. 
  <br />
 
  <br />
 После пересечения финишной черты последним участником, секундомеры отключаются. В протоколе отмечается время прохождения второго подэтапа и начисленные штрафные или призовые баллы. 
  <br />
 
  <br />
 Если участник при освобождении пострадавшего от действия электрического тока попадает под напряжение, то баллы по данному подэтапу команде не начисляются. 
  <br />
 
  <br />
 Если выполнение задания превышает 5-ти минутный лимит времени, то команде начисляются штрафные баллы (20 баллов за каждую минуту превышения лимита времени). 
  <br />
 
  <br />
 6.3.Третий подэтап. &laquo;Оказание первой помощи каждым участником в одной ситуационной задаче&raquo;. Продолжительность &ndash; 5 минут. 
  <br />
 
  <br />
 Заключается в демонстрации знаний и навыков в оказании первой помощи каждым участником в следующих ситуациях: 
  <br />
 
  <br />
 - перелом костей конечностей и таза; 
  <br />
 
  <br />
 - опасные для жизни кровотечения; 
  <br />
 
  <br />
 - потеря сознания; 
  <br />
 
  <br />
 - ранения мягких тканей головы; 
  <br />
 
  <br />
 - травматическая ампутация пальцев кисти; 
  <br />
 
  <br />
 - выпадение внутренних органов наружу; 
  <br />
 
  <br />
 - ожоги. 
  <br />
 
  <br />
 - и др. 
  <br />
 
  <br />
 На место проведения третьего подэтапа вызывается полностью вся команда. Каждый участник берет по одному билету с одной ситуационной задачей. 
  <br />
 
  <br />
 После ознакомления с условиями задачи, участник подходит к линии старта. По команде судьи он приступает к действиям. Во время выполнения своего задания участник может привлечь в помощь одного или двух независимых сотрудников. Привлечение помощников из состава команды запрещается. 
  <br />
 
  <br />
 В случае подсказки со стороны команды, руководителя или представителей команды, участнику, выполняющего задание, баллы не начисляются. 
  <br />
 
  <br />
 Судьи оценивают результат выполнения задания только в отношении участника, зачитавшего условие задачи. Действия помощников не оцениваются. 
  <br />
 
  <br />
 Время, затрачиваемое членом команды на выполнение ситуационной задачи, не должно превышать 5 минут. Далее по такой схеме решают поставленные задачи следующие участники. 
  <br />
 
  <br />
 После прохождения 3-го подэтапа участники покидают площадку и готовятся к прохождению 4-го этапа. 
  <br />
 
  <br />
 6.4.Четвертый подэтап. &laquo;Оказание первой помощи пострадавшим командой из четырех человек&raquo;. Заключается в демонстрации навыков оказания первой помощи пострадавшим с различными травмами, находящимся без признаков жизни, а так же в состоянии клинической смерти. Количество пострадавших до пяти человек, в зависимости от выбранной ситуационной задачи (игры). Продолжительность &ndash; 20 минут. 
  <br />
 
  <br />
 Подэтап проводится на независимых сотрудниках, роботе-тренажере &laquo;ГОША&raquo;, расположенных на полу в позах, соответствующих предполагаемым повреждениям. 
  <br />
 
  <br />
 После подробного инструктажа, по условиям прохождения подэтапа, судья показывает места расположения телефона, средств иммобилизации и иных необходимых материалов и оборудования, обязательно отвечает на все вопросы участников по условиям выполнения задания. После полученных разъяснений группа выстраивается у черты старта. 
  <br />
 
  <br />
 После построения у черты старта старший судья зачитывает условия ситуационной задачи, например: &laquo;После поражения электрическим током произошло массовое падение с высоты. Есть несколько пострадавших, ваша задача &ndash; оказать первую помощь всем и вынести в безопасную зону. В случае если реанимация не будет успешной, данный пострадавший выносится в последнюю очередь без пульса на сонной артерии&raquo;. 
  <br />
 
  <br />
 По команде судьи, с одновременным включением двух секундомеров, команда приступает к оказанию помощи. Участникам необходимо оказать помощь всем пострадавшим и затем с помощью средств транспортировки перенести за условную черту финиша. 
  <br />
 
  <br />
 После пересечения финишной черты последним участником, судьи останавливают секундомеры. 
  <br />
 
  <br />
 В протоколе производится регистрация среднеарифметического времени прохождения подэтапа по показаниям 2-х секундомеров. При оценке времени выполнения задания результат округляется до половины минуты по правилам округления. 
  <br />
 
  <br />
 После построения команды у стартовой черты судьи проводят подробный разбор ошибок, допущенных при выполнении задания, 
  <br />
 и оглашают результат подэтапов №1-4 в баллах. 
  <br />
 
  <br />
 В случае превышения временного норматива выполнения задания на 4 подэтапе команде начисляются штрафные баллы (20 баллов за каждую минуту превышения лимита времени). 
  <br />
 
  <br />
 В случае экономии времени и безошибочном прохождении 2 и 4 подэтапа начисляются призовые балы в размере 2 баллов за каждую сэкономленную минуту времени. 
  <br />
 
  <br />
 После ознакомления команды с результатами прохождения 4-х подэтапов команда заканчивает выступление. Ее руководитель подписывает протокол (Приложение № 1) и команда покидает этап № 4. 
  <br />
 
  <br />
 Общее время для прохождения этапа составляет не более 2 часов. 
