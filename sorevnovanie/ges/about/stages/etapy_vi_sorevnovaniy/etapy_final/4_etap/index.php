  <br />
 4.5.Четвертый подэтап: &laquo;Оказание первой помощи нескольким пострадавшим командой из четырех человек заключается в демонстрации навыков оказания первой помощи пострадавшим с различными травмами, находящимся без признаков жизни, а так же в состоянии клинической смерти. Количество пострадавших до пяти человек, в зависимости от выбранной ситуационной задачи (игры). 
