<p><b>Цель этапа</b> &ndash; проверка знаний участников соревнований, требований нормативно-технических документов, практических навыков их применения путем выявления нарушений при просмотре видеосюжетов.</p>
 
<p> 
  <br />
 </p>
 
<p>Задача участников &ndash; обнаружить и зафиксировать как можно больше нарушений, требований, норм и правил при просмотре видеосюжета за отведенное время.</p>
 
<p> 
  <br />
 </p>
 
<p>Этап проводится на ПЭВМ с использованием программы &laquo;ТВТ Shell&raquo; и модуля &laquo;Видеосюжет&raquo; (Visual ТБ).</p>
 
