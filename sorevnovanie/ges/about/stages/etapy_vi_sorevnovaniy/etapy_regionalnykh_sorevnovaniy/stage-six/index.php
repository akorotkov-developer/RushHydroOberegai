<p><b>Порядок проведения этапа</b></p>
 
<p><b> 
    <br />
   </b></p>
 
<p>Для проведения этапа оборудуются 5 рабочих мест с ПЭВМ, объединенными локальной сетью, и сетевым принтером (одно рабочее место предназначено для судей). На компьютерах устанавливается модуль &laquo;Видеосюжет&raquo; (Visual ТБ). Каждому участнику соревнований устанавливается определенный видеосюжет, который ему предстоит просмотреть. Задание сюжетов составляется с учетом должности участника (роли в команде).</p>
 
<p> 
  <br />
 </p>
 
<p>Видеосюжет разбит на отдельные фрагменты. Во время просмотра очередного фрагмента участник должен оценить его на предмет наличия или отсутствия в нем нарушений НТД.</p>
 
<p> 
  <br />
 </p>
 
<p>При наличии нарушений участник должен нажать на специальную кнопку и отметить нарушение щелчком мышки (если нарушение явно присутствует в кадре) и обязательно набрать в текстовом поле, пользуясь клавиатурой компьютера, описания нарушения в произвольной форме, которое автоматически заносятся в отчет программы. Не засчитываются нарушения, для которых участником не дано достаточное по мнению судей описание, или дано неполное, обобщенное или непонятное описание.</p>
 
<p> 
  <br />
 </p>
 
<p>Нарушения, повторяющиеся в нескольких фрагментах, могут вноситься в протокол только один раз. Повторное выделение нарушения, присутствующего в предыдущих видеофрагментах (кроме особо оговоренных случаев), не приносит участнику дополнительных баллов.</p>
 
