<p><b>Система оценок</b></p>
 
<p><b> 
    <br />
   </b></p>
 
<p>При оценке выступления участника соревнований определяется суммарное количество правильно выявленных в видеосюжете нарушений.</p>
 
<p> 
  <br />
 </p>
 
<p>Под правильно выявленным нарушением следует понимать не только сам факт фиксации нарушения, но также правильная его фиксация в протоколе ‑ выделение стрелкой в кадре и понятное судьям текстовое описание нарушения участником соревнований.</p>
 
<p> 
  <br />
 </p>
 
<p>За квалификацию участником правильных действий персонала (нормального состояния оборудования) как нарушение, судейская бригада фиксирует за данное определение оценку 0 баллов с добавлением в протокол соответствующего комментария (&laquo;Нарушения нет&raquo;). 
  <br />
 </p>
 
<p> 
  <br />
 </p>
 
<p>При оценке фрагментов, содержащих нарушения, следует учитывать, что некоторые нарушения являются &laquo;сквозными&raquo;, т.е. проходят через весь видеосюжет. Например, это может быть отсутствие каски у одного из персонажей или отсутствие надписи на оборудовании. ФИКСИРОВАТЬ ПОДОБНЫЕ &laquo;СКВОЗНЫЕ&raquo; НАРУШЕНИЯ СЛЕДУЕТ ТОЛЬКО ПРИ ИХ ПЕРВОМ ПОЯВЛЕНИИ НА ЭКРАНЕ. Повторно выявленные нарушения &laquo;сквозного&raquo; характера не учитываются, судьи выставляют за данное нарушение оценку 0 баллов с добавлением в протокол соответствующего комментария (&laquo;Повтор&raquo;).</p>
 
<p> 
  <br />
 </p>
 
<p>Повторные (однотипные) нарушения следует учитывать только один раз, даже если они относятся к разному оборудованию, разным персонажам видеосюжета, совершены в разное время, в разных местах и т.д. Повторно выявленные однотипные нарушения не учитываются, а судья проставляет за данное нарушение оценку &quot;0&quot; с добавлением в протокол соответствующего комментария (&laquo;Повтор&raquo;).</p>
 
<p> 
  <br />
 </p>
 
<p>Максимально возможное количество баллов, которое может набрать команда на этапе &ndash; 400 баллов.</p>
 
<p> 
  <br />
 </p>
 
<p>После прохождения этапа всеми командами определяется команда, которая имеет наибольшее количество правильно выявленных нарушений. Этой команде в зачет выставляется максимальное количество баллов на этапе.</p>
 
<p> 
  <br />
 </p>
 
<p>При наличии у соревнующихся команд равного количества баллов лучшим результатом на этапе считается тот результат, который получен за наименьшее количество времени, затраченного командой на прохождение этапа. При равенстве количества обнаруженных нарушений и времени прохождения, командам присваивается равное количество баллов.</p>

