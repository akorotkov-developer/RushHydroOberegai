<p><b>Состав заданий</b></p>
 
<p><b> 
    <br />
   </b></p>
 
<p>Для каждого участника соревнующейся команды предварительно разрабатывается видеосюжет, во время просмотра которого необходимо выявить имеющиеся в нем нарушения НТД.</p>
 
<p> 
  <br />
 </p>
 
<p>Видеосюжеты сняты на действующей электростанции, их сценарии утверждены Главным судьей соревнования.</p>
 
