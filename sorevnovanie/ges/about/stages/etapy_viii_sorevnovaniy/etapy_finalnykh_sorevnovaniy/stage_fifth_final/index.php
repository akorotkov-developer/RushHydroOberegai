<p> </p>
 
<ul> 
  <li>&laquo;Правил технической эксплуатации электрических станций и сетей Российской Федерации&raquo;, утвержденных приказом Минэнерго России от 19.06.2003 № 229;</li>
 
  <li>«Правил предотвращения развития и ликвидации нарушений нормального режима электрической части энергосистем», СТО 59012820.29.240.007-2008, распоряжение ОАО «СО ЕЭС» от 24.09.2008 №114р;</li>
 
  <li>«Правил переключений в электроустановках», СТО 59012820.29.020.005-2011 от 25.10.2011, в редакции приказов АО «СО ЕЭС» №201 от 29.07.2014, №48 от 20.02.2017;</li>
 
  <li>«Инструкции по переключениям в электроустановках», СО 153-34.20.505-2003, утвержденная приказом Минэнерго России от 30.06.2003 г., №266;</li>
 
  <li>«Порядка отдачи и регистрации стандартных документируемых диспетчерских команд, распоряжений, разрешений и сообщений, используемых диспетчерским персоналом АО «СО ЕЭС» и его филиалов при управлении режимами работы объектов генерации участников оптового рынка и внешними перетоками», утвержденным Заместителем Председателя Правления АО «СО ЕЭС» «19» апреля 2018 года;</li>
 
  <li>Инструкции о порядке ведения оперативных переговоров диспетчерским персоналом ЦДУ, утвержденная Директором по управлению режимами ЕЭС - главным диспетчером ОАО «СО ЕЭС» «11» ноября 2014 года.</li>
 </ul>
 
<p></p>
 
