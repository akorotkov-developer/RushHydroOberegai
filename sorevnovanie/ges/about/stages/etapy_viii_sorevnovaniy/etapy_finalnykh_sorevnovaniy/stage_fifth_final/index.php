  <br />
 </p>
 
<p><b>Состав задания. </b></p>
 
<p><b> 
    <br />
   </b></p>
 
<p>Этап №5 состоит из двух подэтапов: </p>
 
<p> 
  <br />
 </p>
 
<p> </p>
 
<ul> 
  <li><i style="font-size: 12.8px;">На подэтапе №1 (Работа на Тренажере)</i><span style="font-size: 12.8px;"> участники команд должны выполнить задание, предусмотренное сценарием по ведению заданного режима работы ГЭС/ГАЭС с помощью Тренажера;</span></li>
 
  <li><i style="font-size: 12.8px;">На подэтапе №2 (Решение теоретической Задачи)</i><span style="font-size: 12.8px;"> каждый участник команды должен самостоятельно решить теоретическую задачу в соответствии с должностными обязанностями оперативного персонала гидроэлектростанций и тематикой в рамках реализации оперативно-технологического управления оборудованием ГЭС (далее – Задача).</span></li>
 </ul>
 
<p></p>
 
