<p align="justify"> </p>
 
<p align="justify"> </p>
 
<p align="justify">Основной задачей мандатной комиссии является проверка соответствия квалификации участников и документов, представленных командами, условиям &laquo;Положения о проведении Шестых Всероссийских соревнований оперативного персонала гидроэлектростанций&raquo;, Положений о проведении этапов и действующих нормативно-технических документов.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">Состав мандатной комиссии назначается, и утверждается, Главным судьей соревнований в составе Председателя мандатной комиссии и двух членов мандатной комиссии. Председатель и члены мандатной комиссии назначаются из числа сотрудников Департамента технической инспекции и строительного контроля ОАО &laquo;РусГидро&raquo;. По решению Главного судьи Соревнований в случае необходимости членами мандатной комиссии могут быть назначены специалист по охране труда и/или начальник Службы охраны труда (аналогичного подразделения) предприятия, на базе которого проводятся соревнования.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">Команды проходят мандатную комиссию по заранее подготовленному секретариатом соревнований графику, доведенному до руководителей команд по прибытии на место проведения соревнований.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">Руководители команд представляют в мандатную комиссию следующие документы:</p>
 
<p align="justify">- протоколы и удостоверения об аттестации руководителей и специалистов в соответствии с приказом Ростехнадзора от 29.02.2007 № 37; </p>
 
<p align="justify">- удостоверения о проверке знаний формы, не противоречащей Приложению 2 &laquo;ПОТ ЭУ Правила охраны труда при эксплуатации электроустановок, утв. Приказом Министерства труда и социальной защиты Российской Федерации от 24 июля 2013 № 328н&raquo; с отметками о проверке знаний и предоставлении права на производство специальных работ вместе с заверенными копиями протоколов последних проверок знаний норм и правил (ПТЭ, ПОТ РМ, ППБ, ПУиБЭ сосудов, ФНП ПС и т.д.);</p>
 
<p align="justify">- копии распорядительных документов о допуске к обслуживанию сосудов (ответственных за исправное состояние и безопасную эксплуатацию &ndash; при наличии ответственности);</p>
 
<p align="justify">- именные инструкции по оказанию первой помощи при несчастных случаях на производстве (утв. Техническим директором ОАО РАО &laquo;ЕЭС России&raquo; 21.06.2007, согласована ОО &laquo;Всероссийский профсоюз&raquo;, ГНИИИ Военной медицины Минобороны РФ, Институтом охраны труда и технического аудита);</p>
 
<p align="justify">- действующий страховой полис обязательного медицинского страхования на каждого члена команды;</p>
 
<p align="justify">- справку службы кадров о прохождении обязательного медицинского осмотра (или выписку из Акта обязательного периодического медосмотра, или заверенная копия справки/заключения медучреждения);</p>
 
<p align="justify">- копию перечня средств защиты в конкретной ДЭУ ГЭС, утвержденного главным инженером предприятия;</p>
 
<p align="justify">- копии сертификатов на комплекты для защиты от электрической дуги, копии сертификатов (деклараций о соответствии) на средства защиты согласно представленному перечню средств защиты. В случае отсутствия на предприятии сертификатов на средства защиты руководитель команды предъявляет протоколы испытания СИЗ или копии листов журнала учета и испытания СИЗ, содержащих сведения о проведенных испытаниях СИЗ.</p>
 
<p align="justify">Копии вышеперечисленных документов должны быть заверены подписью и печатью службы кадров предприятия. </p>
 
<p align="justify">Участники команд (кроме руководителя команды и сопровождающего) должны представить мандатной комиссии экипировку в летние комплекты для защиты от электрической дуги. </p>
 
<p align="justify">При необходимости руководителем команды даются пояснения по содержанию документов и качеству их оформления. </p>
 
<p align="justify">За выявленные нарушения в оформлении документов начисляются штрафные баллы.</p>
 
<p align="justify">По результатам проверки каждой команды мандатная комиссия составляет протокол (приложение А) с отражением замечаний, штрафов и решения о возможности допуска команды к соревнованиям. </p>
 
<p align="justify">По окончании проверки для допуска к участию в Соревнованиях всех команд мандатная комиссия составляет итоговый протокол на основании протоколов по каждой команде (приложение Б). </p>
 
<p align="justify">Жеребьёвка команд проводится на основании итогового протокола результатов проверки команд для допуска к участию в Соревнованиях. По результатам жеребьёвки составляется протокол (приложение В). </p>
 
