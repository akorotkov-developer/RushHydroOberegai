<p align="center"><b>6. </b><b>Судейство этапа</b></p>
 
<p> </p>
 
<br />
 
<div align="justify">6.1. Руководитель команды должен присутствовать при разборе прохождения его командой этапа и подписании протокола. Участники команды при разборе не присутствуют. При возникновении вопросов к отдельным участникам, судьи могут вызывать их через руководителя команды. 
  <br />
 
  <br />
 6.2. Для оценки действий соревнующихся назначается судейская бригада из двух человек. Возглавляет бригаду старший судья. 
  <br />
 
  <br />
 6.3. На рабочих местах судейской комиссии шестого этапа должны быть телефоны для связи с Главной судейской комиссией и секретариатом, Положение о проведении соревнований, настоящее Положение, список участников соревнования, график прохождения этапов, шаблоны протоколов этапа. 
  <br />
 
  <br />
 6.4. Судейская бригада на этапе осуществляет: 
  <br />
 
  <br />
 - судейство на этапе соревнования; 
  <br />
 
  <br />
 - выполнение графика проведения соревнования; 
  <br />
 
  <br />
 - инструктаж соревнующихся бригад непосредственно перед началом соревнования на месте проведения этапа; 
  <br />
 
  <br />
 - ознакомление участников с рабочими местами, оргтехникой и порядком работы с компьютерной программой; 
  <br />
 
  <br />
 - организацию допуска участников команды на рабочие места; 
  <br />
 
  <br />
 - организацию просмотра видеосюжетов; 
  <br />
 
  <br />
 - организацию устранения неисправностей оргтехники и программного обеспечения; 
  <br />
 
  <br />
 - контроль за поведением соревнующихся, ограждение их от посторонних лиц и помех; 
  <br />
 
  <br />
 - контроль за поведением участников соревнования и принятие решений об их снятии с этапа за возможные нарушения условий соревнования; 
