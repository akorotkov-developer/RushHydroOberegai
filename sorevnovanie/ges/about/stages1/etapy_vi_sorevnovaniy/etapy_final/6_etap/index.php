  <br />
 
  <br />
 &middot; баллы и затраченное время на подэтапе 6.1; 
  <br />
 
  <br />
 &middot; количество нарушений и затраченное время на подэтапе 6.2. 
  <br />
 
  <br />
 6.6. Итоговый протокол этапа после подписи судьями этапа передаётся в Главную судейскую комиссию не позднее 1 часа после окончания этапа. 
  <br />
 
  <br />
 6.7. Оценка по этапу принимается большинством голосов судейской бригады. В случае расхождения мнений, любой из судей имеет право сообщить свое мнение в Главную судейскую комиссию, которое рассматривается до подведения окончательных итогов. 
