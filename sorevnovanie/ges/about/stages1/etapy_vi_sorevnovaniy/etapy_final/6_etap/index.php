<p align="center"><b>1. </b><b>Общие положения</b></p>
 
<p><b> </b></p>
 
<br />
 
<div align="justify">1.1. Цель этапа &ndash; проверка знаний участников соревнований требований нормативно-технических документов, практических навыков их применения для безопасной эксплуатации и ремонта оборудования. 
  <br />
 
  <br />
 1.2. Задачи участников на этапе: 
  <br />
 
  <br />
 &middot; проверка правильности заполнения нарядов-допусков; 
  <br />
 
  <br />
 &middot; обнаружение и фиксация как можно большего числа нарушений требований норм и правил при просмотре видеосюжета за отведенное время. 
  <br />
 
  <br />
 1.3. Этап проводится на ПЭВМ с использованием программы &laquo;ТВТ Shell&raquo; и модулей &laquo;Видеосюжет&raquo; и &laquo;Наряд&raquo; (Visual ТБ). 
  <br />
 
  <br />
 1.4. Этап разделен на 2 подэтапа: 
  <br />
 
  <br />
 &middot; подэтап 6.1 (&laquo;Наряд&raquo;) &ndash; проверка знаний и умений применять нарядно-допускную систему для безопасной эксплуатации и ремонта оборудования; 
  <br />
 
  <br />
 &middot; подэтап 6.2 (&laquo;Видеосюжет&raquo;) &ndash; проверка знаний и умений выявлять нарушения НТД при просмотре видеосюжета. 
  <br />
 </div>
 
<p> </p>
 
<p align="center"><b>2. </b><b>Требования к участникам этапа</b></p>
 
<p><b> </b></p>
 
<br />
 
<div align="justify">2.1. Состав и требования к участникам команды устанавливаются в соответствии с Положением о поведении соревнований. К прохождению этапа допускается команда прошедшая Мандатную комиссию и допущенная ею к участию в соревнованиях. 
  <br />
 
  <br />
 2.2. Участники, прибывшие для прохождения этапа соревнований должны иметь при себе удостоверения установленного образца с отметками о проверках знаний, группу по электробезопасности не ниже &ndash; III, на одежде (единого образца для комплексной бригады ГЭС) должна присутствовать эмблема предприятия. 
  <br />
 </div>
 
<p> </p>
 
<p align="center"><b> 
    <br />
   </b></p>
 
<p align="center"><b>3. </b><b>Состав заданий</b></p>
 
<p><b> </b></p>
 
<br />
 
<div align="justify">3.1. На подэтапе 6.1 (&laquo;Наряд&raquo;) задача участников проверить правильность заполнения одного наряда-допуска по темам, связанными с работами в соответствующем подразделении. В наряде может быть заложено от 5 до 10 нарушений. 
  <br />
 
  <br />
 3.2. На подэтапе 6.2 (&laquo;Видеосюжет&raquo;) для каждого участника соревнующейся команды предварительно разрабатывается видеосюжет, во время просмотра которого необходимо выявить имеющиеся в нем нарушения НТД. 
  <br />
 
  <br />
 3.3. Видеосюжеты сняты на действующей электростанции, их сценарии утверждены Главным судьей соревнования. 
  <br />
 </div>
 
<p align="justify"> </p>
 
<p align="center"><b>4. </b><b>Порядок проведения этапа</b></p>
 
<p><b> </b></p>
 
<br />
 
<div align="justify">4.1. Для проведения этапа оборудуются 5 рабочих мест с ПЭВМ, объединенными локальной сетью, и сетевым принтером (одно рабочее место предназначено для судей). На компьютерах устанавливаются модули &laquo;Видеосюжет&raquo; и &laquo;Наряд&raquo; (Visual ТБ). Каждому участнику соревнований устанавливается определенные наряд и видеосюжет, с которыми ему предстоит работать. Задание на этап составляется с учетом должности участника (роли в команде). 
  <br />
 
  <br />
 4.2. До начала проведения этапа соревнований проводится тренировка в целях ознакомления с работой программы. Каждой команде предоставляется время по графику, составленному организаторами соревнований. <b> </b>Команды, прибывшие на этап для его прохождения, должны быть обучены порядку работы с программами &laquo;Видеосюжет&raquo; и &laquo;Наряд&raquo; (Visual ТБ). В соответствии с графиком проведения соревнований участники прибывают на этап всей командой к указанному времени для одновременного прохождения этапа. 
  <br />
 
  <br />
 4.3. По прибытии команды на этап начальник смены станции (НСС) докладывает старшему судье о готовности команды к прохождению этапа. 
  <br />
 
  <br />
 4.4. Руководитель команды и сопровождающий (с разрешения старшего судьи этапа) могут присутствовать при прохождении командой этапа. 
  <br />
 
  <br />
 4.5. Руководитель команды и сопровождающий не имеют права вмешиваться в ход прохождения этапа, комментировать действия команды, вступать с участниками или судьями в переговоры. При нарушении этого требования судейская бригада имеет право удалить руководителя команды и сопровождающего с этапа, сняв при подведении итогов прохождения этапа командой по два выявленных нарушения с каждого участника команды. 
  <br />
 
  <br />
 4.6. Судьи проверяют у каждого участника наличие квалификационного удостоверения и соответствие участников протоколу Мандатной комиссии, и заносят соответствующие данные в протокол. 
  <br />
 
  <br />
 4.7. По окончании проверки все участники команды занимают рабочие места за компьютерами согласно табличкам с наименованием должностей участников. Судьи проводят инструктаж (не более 5 минут) участников команд о порядке прохождения этапа. По команде судей участники приступают к прохождению этапа. 
  <br />
 
  <br />
 4.8. Этап проводится в строгой очередности: подэтап 6.1 &laquo;Наряд&raquo;, подэтап 6.2 &laquo;Видеосюжет&raquo;. 
  <br />
 
  <br />
 4.9. Суммарное время на выполнение всех двух подэтапов &ndash; 50 мин. Затрачиваемое время на каждый подэтап участник определяет сам. 
  <br />
 
  <br />
 4.10. Перед началом прохождения подэтапа 6.1 судьи выставляют каждому участнику контрольное время, равное 50 мин. После окончания подэтапа 6.1 судьи отмечают время, затраченное на подэтап 6.1, и выставляют в тестовой программе участнику на подэтап 6.2 оставшееся время от 50 мин. 
  <br />
 
  <br />
 4.11. В случае превышения суммарного времени на выполнение этапа (п. 4.9) на подэтапе 6.1, подэтап 6.2 этим участником считается не пройдённым и участник получает за них оценку 0 (ноль). 
  <br />
 
  <br />
 4.12. Все участники выполняют работу на компьютерах с использованием программ, обеспечивающих контроль времени прохождения этапа и ведение отчета о выполненных действиях. 
  <br />
 
  <br />
 4.13. При работе на подэтапе 6.1 (&laquo;Наряд&raquo;) участник должен просмотреть заполненный наряд-допуск на экране ЭВМ и выделить отдельные области электронного представления наряда-допуска, где, по его мнению, есть отступления от требований нормативно-технической документации. Выделение области производится путем щелчка мышки по этой области. Повторный щелчок мышки по ранее отмеченной области снимает ее выделение. 
  <br />
 
  <br />
 4.14. Видеосюжет, используемый на подэтапе 6.2 (&laquo;Видеосюжет&raquo;), разбит на отдельные фрагменты. Во время просмотра очередного фрагмента участник должен оценить его на предмет наличия или отсутствия в нем нарушений НТД. 
  <br />
 
  <br />
 4.15. При наличии нарушений участник должен нажать на специальную кнопку и отметить нарушение щелчком мышки (если нарушение явно присутствует в кадре) и обязательно набрать в текстовом поле, пользуясь клавиатурой компьютера, описания нарушения в произвольной форме, которое автоматически заносятся в отчет программы. Не засчитываются нарушения, для которых участником не дано достаточное, по мнению судей, описание, или дано неполное, обобщенное или непонятное описание. 
  <br />
 
  <br />
 4.16. Нарушения, повторяющиеся в нескольких фрагментах, вносятся в протокол только один раз. Повторное выделение нарушения, присутствующего в предыдущих видеофрагментах (кроме особо оговоренных случаев), не приносит участнику дополнительных баллов. 
  <br />
 
  <br />
 4.17. Общее время прохождения этапа &ndash; 2 часа. Это время включает в себя просмотр видеосюжетов участниками, подведение судьями итогов прохождения командой этапа. Максимальное время, отводимое участнику на работу на ПЭВМ с нарядами и видеосюжетом &ndash; 50 минут. После окончания времени отведенного на прохождение задания, участники команды покидают помещение этапа. Команда должна проходить этап в соответствии со следующим графиком: 
  <br />
