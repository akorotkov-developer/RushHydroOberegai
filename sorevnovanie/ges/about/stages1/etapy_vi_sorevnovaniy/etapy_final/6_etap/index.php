<div align="justify">4.18. Каждый участник выполняет задания индивидуально и не имеет право обсуждать свои действия с другими членами бригады. Пользоваться технической литературой и конспектами запрещается. При нарушении данных требований работа участника по выполнению задания прекращается судейской бригадой, участник удаляется с места проведения этапа, количество нарушений выявленных им принимается равным 0 в итоговом протоколе. 
