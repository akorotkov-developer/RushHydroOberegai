<p align="center"><b>5. </b><b>Система оценок подэтапа 6.1 (&laquo;Наряды&raquo;)</b></p>
 
<p><b> </b></p>
 
<br />
 
<div align="justify">5.1. Общее максимальное количество баллов за подэтап, которое может получить команда, составляет 40 баллов (по 10 баллов каждому участнику этапа). 
  <br />
 
  <br />
 5.2. Работа команды автоматически оценивается компьютерной программой, включенной в состав тестирующего комплекса и учитывающей ошибки и нарушения в действиях операторов. 
  <br />
 
  <br />
 5.3. За каждую допущенную ошибку при проверке правильности заполнения наряда-допуска снимается по 2 балла. 
  <br />
 
  <br />
 5.4. За ошибку при проверке правильности заполнения наряда-допуска принимается: 
  <br />
 
  <br />
 &middot; выделение поля наряда-допуска в котором нет нарушения требования НТД; 
  <br />
 
  <br />
 &middot; не выделение поля наряда-допуска в котором есть нарушения требования НТД. 
  <br />
 
  <br />
 5.5. При разборе подэтапа (после прохождения этапа всеми участниками) участник может документально обосновать на основании НТД выделение им поля наряда-допуска, которое не было заложено в программу. В случае правильного обоснования баллы, снятые за это выделение, возвращаются. 
  <br />
 
  <br />
 5.6. При достижении контрольного времени отведенного на подэтап работа контролирующей программы автоматически прекращается и за каждую ошибку (п. 5.4) снимается по 2 балла, при этом итоговая оценка за подэтап ограничена снизу нулевой оценкой. 
  <br />
 </div>
 
<br />
 
<p><b> </b></p>
 
<p align="center"><b>6. </b><b>Система оценок подэтапа 6.2 (&laquo;Видеосюжет&raquo;)</b></p>
 
<p><b> </b></p>
 
<br />
 
<div align="justify">6.1. При оценке выступления участника соревнований определяется суммарное количество правильно выявленных в видеосюжете нарушений. 
  <br />
 
  <br />
 6.2. Под правильно выявленным нарушением следует понимать не только сам факт фиксации нарушения, но также правильная его фиксация в протоколе ‑ выделение стрелкой в кадре и понятное судьям текстовое описание нарушения участником соревнований. 
  <br />
 
  <br />
 6.3. За квалификацию участником правильных действий персонала (нормального состояния оборудования) как нарушение, судейская бригада фиксирует за данное определение оценку 0 баллов с добавлением в протокол соответствующего комментария (&laquo;Нарушения нет&raquo;). 
  <br />
 
  <br />
 6.4. При оценке фрагментов, содержащих нарушения, следует учитывать, что некоторые нарушения являются &laquo;сквозными&raquo;, т.е. проходят через весь видеосюжет. Например, это может быть отсутствие каски у одного из персонажей или отсутствие надписи на оборудовании. ФИКСИРОВАТЬ ПОДОБНЫЕ &laquo;СКВОЗНЫЕ&raquo; НАРУШЕНИЯ СЛЕДУЕТ ТОЛЬКО ОДИН РАЗ. Повторно выявленные нарушения &laquo;сквозного&raquo; характера не учитываются, судьи выставляют за данное нарушение оценку 0 баллов с добавлением в протокол соответствующего комментария (&laquo;Повтор&raquo;). 
  <br />
 
  <br />
 6.5. Повторные (однотипные, одинаковые) нарушения также следует учитывать только один раз, даже если они относятся к разному оборудованию, разным персонажам видеосюжета, совершены в разное время, в разных местах и т.д. Повторно выявленные однотипные нарушения не учитываются, а судья проставляет за данное нарушение оценку &quot;0&quot; с добавлением в протокол соответствующего комментария (&laquo;Повтор&raquo;). 
  <br />
 
  <br />
