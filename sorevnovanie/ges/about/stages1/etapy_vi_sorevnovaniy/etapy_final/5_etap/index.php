  <div align="center"><b> 
      <br />
     7. Система оценок этапа</b> 
    <br />
   </div>
 
  <br />
 
  <br />
 
  <br />
 7.1. Номинальное количество баллов этапа (без штрафных баллов) составляет 400 баллов. 
  <br />
 
  <br />
 7.2. По окончании прохождения командой этапа судейская бригада фиксирует суммарное командное время и индивидуальные времена прохождения под этапов участниками команды с учетом штрафных секунд. По итогам выступления всех команд на этапе, команда, набравшая суммарно наименьшее количество секунд, получает в зачет 400 баллов. Время лучшей команды пересчитывается на каждый балл из полученных 400 (номинал в секундах одного балла), все остальные команды получают баллы за вычетом секунд отставания от лучшей команды, переведенных в баллы через номинал. 
  <br />
 
  <br />
 7.3. Рейтинг среди участников по каждой профессии на этапе формируется аналогично подведению итогов в командном зачете. 
  <br />
 
  <br />
 7.4. Штрафные секунды начисляются за: 
  <br />
 
  <br />
