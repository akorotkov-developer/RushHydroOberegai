  <div align="center"><b>6. Порядок проведения пятого этапа</b> 
    <br />
   </div>
 
  <br />
 &laquo;Пожарная эстафета&raquo;. 
  <br />
 
  <br />
 
  <br />
 
  <br />
 6.1.По прибытии команды в назначенное по графику времяна территорию специально подготовленного полигона, где будет проводиться этап, руководитель бригады &mdash; начальник смены станции (ГЭС) докладывает старшему судье о готовности бригады к выполнению действий на этапе.Руководитель бригады доводит до сведения судей информацию, кто из членов команды будет участвовать на подэтапах эстафеты (получение номера). Номера для участников всех бригад одинаковые по должностям, порядок выступления выбирает судейская бригада до выступления первой команды на этапе. Участники, выступающие на четвертом дистанционном отрезке эстафеты должны применять боевую одежду пожарного 1-го уровня защиты, пожарный пояс, каску пожарного, краги (перчатки пожарного). 
  <br />
 
  <br />
 При прохождении подэтапа боевая одежда должна быть застегнута, ремень затянут, каска застегнута, краги одеваются на перчатки. 
  <br />
 
  <br />
 6.2. Судейская бригада проверяет: 
  <br />
 
  <br />
 &middot; наличие у каждого участника квалификационного удостоверения; 
  <br />
 
  <br />
 &middot; соответствие формы и экипировки требованиям безопасности проведения работ.Не допускаются к соревнованиям участники в открытой, поврежденной и не застегнутой одежде. 
  <br />
 
  <br />
 6.3. По окончании проверки участников старший судья проводит инструктаж по безопасности и о порядке проведения этапа. Перед началом этапа команда находится за линией старта. 
  <br />
 
  <br />
 6.4. По команде старшего судьи этапа команда приступает к выполнению задания на этапе. Судьи при помощи секундомеров контролируют время прохождения каждого из участников эстафеты, а так же качество выполнения ими задания. Передача эстафеты участниками осуществляется при помощи ствола, используемого в качестве эстафетной палочки. Ствол во время движения фиксируется участником на ремне (за спиной). Зона передачи ствола (эстафеты) - 3метрав зоне передачи. 
  <br />
 
  <br />
 
  <br />
 
  <br />
 <i>&laquo;Первый подэтап</i>&quot;. Порядок сообщения о пожаре и правильное оформление допуска на тушение пожара. 
  <br />
 
  <br />
 Дается вводная судьи на подэтапе в соответствии с выбранным заданием.Например,<i>&quot;Пожар в кабельном коробе с силовыми кабелями 6 кВ и кабелями управления 0,4 кВ на &laquo;Энской ГЭС&raquo;.</i> 
  <br />
 
  <br />
 После изучения участником вводной, судьей дается команда &quot;Приступить к выполнению задания&quot; и судьями включаются секундомеры. 
  <br />
 
  <br />
 Участник стартует от места расположения старта с положения, стоя со стволом, преодолевает дистанцию 50 метров до места расположения телефона.Начальник смены станции по телефону &quot;01&quot; должен сообщить: 
  <br />
 
  <br />
 - свою фамилию, номер телефона, с которого делается сообщение о пожаре, сообщить точный адрес и наименование объекта, место возникновения пожара, вероятную возможность угрозы людям и распространения пожара, действия персонала по ликвидации пожара. 
  <br />
 
  <br />
 Затем пробегает в зону передачи эстафеты и передает эстафету (ствол) второму номеру. Фраза сообщения выбирается руководителем бригады методом жеребьевки при проведении инструктажа судьями. 
  <br />
 
  <br />
 После передачи эстафеты (ствола) направляется к судейскому столу для оформления допуска на тушение пожара в соответствии с выбранным заданием и карточкой пожаротушения Энской ГЭС. Задание должно быть выполнено в течение 5 минут, и при его правильном выполнении участник получает 10 баллов дополнительно. 
  <br />
 
  <br />
 <i>&laquo;Второй подэтап&raquo;.</i> Участник, приняв эстафету (ствол), фиксирует ствол за ремень,подбегает к рукавам, берет их,и начинает бежать в сторону разветвления, при этом,пробежав 3-5 метров, соединяет между собой нижние головки рукавов, и бросает их в сторону, продолжая бежать к разветвлению. 
  <br />
 
  <br />
 Подбежав к разветвлению, правой (левой) рукой присоединяет рукавную линию, и продолжает бежать, затем берет из - за спины ствол и присоединяет его с рукавной линией, и продолжает следовать до выделенной линии. Кладёт собранную рукавную линию на землю за линией (фиксация выполнения действия), затем отсоединяет ствол, и передает эстафету (ствол) третьему номеру команды. 
  <br />
 
  <br />
 <i>&laquo;Третий подэтап&raquo;.</i> Участник, приняв эстафету (ствол), пробегает 50 метров, кладет эстафетную палочку на землю,берет ствол с проложенной рукавной линии, находящейся под давлением, и струей воды из ствола поражает мишень,расположенную в 10м. После попадания в мишень, участник перекрывает ствол,кладёт его на землю, забирает эстафетную палочку и, завершая свой подэтап, передает эстафету (ствол) четвертому номеру команды. 
  <br />
 
  <br />
 <i>&laquo;Четвертый подэтап&raquo;.</i> Участник, приняв эстафету (ствол), пробегает 40 метров, берет основной огнетушитель,пробегает 10 метров до противня, приводит огнетушитель в действие, и приступает к тушению горящей жидкости в противне.После полного тушения горящей жидкости участник кладет огнетушитель на землю и бежит на финиш.В случае не срабатывания огнетушителя, участник бригады должен воспользоваться резервным, заранее приготовленным огнетушителем, размещенным недалеко от противня. 
  <br />
 
  <br />
 Запрещается кидать огнетушитель в противень после окончания тушения.Выбор основного и резервного огнетушителя (ОУ-5 или ОП-5) осуществляется участником 4-го подэтапа до старта команды. 
  <br />
 
  <br />
 <i>Внимание:</i> Жидкость в противне поджигается заблаговременно за 20 секунд до начала 4-го подэтапа. Противень заливается жидкостью в следующей последовательности: вода - 40 л., осветительный керосин (дизтопливо) - 6 л., автомобильный бензин - 0,5 л. 
  <br />
 
  <br />
 В случае, если огнетушитель не сработал из-за технических неисправностей (по вине завода-изготовителя) допускается повторное выполнение работ на этапе. Несрабатывание огнетушителя по вине завода изготовителя определяется судейской бригадой в каждом конкретном случае. 
  <br />
 
  <br />
 6.6.Оглашение итогов прохождения этапа. 
  <br />
 
  <br />
 6.6.1. Через 5 минут после окончания прохождения этапа, бригада приглашается к судейскому столу для оглашения итогового результата. 
  <br />
 
  <br />
 6.6.2. После ознакомления с итоговым результатом,руководитель команды подписывает протокол, и вместе с командой покидает полигон. 
  <br />
 
  <br />
 6.6.3. Общее время для прохождения этапа не должно превышать 15 минут. 
  <br />
 
