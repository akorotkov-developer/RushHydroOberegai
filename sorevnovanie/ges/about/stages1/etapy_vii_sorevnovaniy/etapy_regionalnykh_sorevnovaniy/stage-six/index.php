   </b></p>
 
<p style="text-align: justify;">Для проведения этапа оборудуются 5 рабочих мест с ПЭВМ, объединенными локальной сетью, и сетевым принтером (одно рабочее место предназначено для судей). На компьютерах устанавливается модуль «Видеосюжет» (Visual ТБ). Каждому участнику соревнований устанавливается определенный видеосюжет, который ему предстоит просмотреть. Задание сюжетов составляется с учетом должности участника (роли в команде). </p>
 
