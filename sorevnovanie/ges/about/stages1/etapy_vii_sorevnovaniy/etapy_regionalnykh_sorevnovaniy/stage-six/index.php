<p style="text-align: justify;">Видеосюжет разбит на отдельные фрагменты. Во время просмотра очередного фрагмента участник должен оценить его на предмет наличия или отсутствия в нем нарушений НТД. </p>
 
<p style="text-align: justify;"> 
  <br />
 </p>
 
<p> </p>
 
<p style="text-align: justify;">При наличии нарушений участник должен нажать на специальную кнопку и отметить нарушение щелчком мышки (если нарушение явно присутствует в кадре) и обязательно набрать в текстовом поле, пользуясь клавиатурой компьютера, описания нарушения в произвольной форме, которое автоматически заносятся в отчет программы. Не засчитываются нарушения, для которых участником не дано достаточное по мнению судей описание, или дано неполное, обобщенное или непонятное описание. </p>
 
<p style="text-align: justify;"> 
