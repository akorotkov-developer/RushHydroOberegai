  <br />
</p>

<p style="text-align: justify;">4.3. Судейские бригады совместно с разработчиками программно-технических средств, задействованных на этапах Соревнований, определяют перечень заданий, выполняемых участниками Соревнований, разрабатывают положения о проведении этапов, обеспечивают доработку и приемку программных средств, оборудования и средств тренажерной подготовки, используемых при выполнении заданий этапов. </p>
 
<p style="text-align: justify;">
  <br />
</p>

<p style="text-align: justify;">4.4. Положения о проведении этапов Соревнований утверждаются Главным судьей Соревнований, и рассылаются участникам не позднее 30 дней до начала Соревнований. </p>
 
<p style="text-align: justify;">
  <br />
</p>

<p style="text-align: justify;">4.5. Финансирование подготовки и проведения Соревнований осуществляется в соответствии со сметой затрат на подготовку и проведение Соревнований филиалом ПАО «РусГидро» – «КорУнГ», в том числе за счет взносов участников Соревнований. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">4.6. Вопросы, не регламентируемые настоящим Положением, выносятся на рассмотрение Оргкомитетом либо Главной судейской комиссии. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p> </p>
 
<p align="center"><b>5. </b><b>Этапы Соревнований. </b></p>

<p align="center"><b>
    <br />
  </b></p>
 
<p> </p>
 
<p style="text-align: justify;">5.1. Соревнования по регионам «Запад» и «Восток» проводятся в 6 этапов: </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">1 этап. Проверка знаний нормативно-технических документов (НТД) и проверка умения выявлять отступления от требований НТД с использованием ПЭВМ и специализированного программного обеспечения. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">Включает проверку знаний НТД и теоретической подготовки, практических навыков их применения для безопасной эксплуатации и ремонта оборудования. </p>
 
<p style="text-align: justify;">
  <br />
</p>

<p style="text-align: justify;"><span style="font-size: 12.8px;">2 этап. Производство переключений в электроустановках.</span></p>
 
<p style="text-align: justify;">
  <br />
</p>

<p style="text-align: justify;">Включает выполнение на корпоративном тренажере технологических операций по переключениям в электроустановках. </p>
 
<p style="text-align: justify;">
  <br />
</p>

<p style="text-align: justify;">3 этап. Противоаварийная тренировка. </p>
 
<p style="text-align: justify;">
  <br />
</p>

<p style="text-align: justify;">Включает выполнение на корпоративном тренажере технологических операций по управлению электротехническим оборудованием в нормальных режимах эксплуатации и при возникновении нарушений в работе оборудования ГЭС. </p>
 
<p style="text-align: justify;">
  <br />
</p>

<p style="text-align: justify;">4 этап. Оказание доврачебной помощи пострадавшим на производстве. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">Определяется уровень подготовки персонала по оказанию доврачебной помощи пострадавшим при несчастных случаях. </p>
 
<p style="text-align: justify;">
  <br />
</p>

<p style="text-align: justify;">5 этап. Ведение энергетического режима работы ГЭС. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">Включает выполнение на корпоративном тренажере технологических операций по ведению электроэнергетического режима и расчета водного режима. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">6 этап. Ликвидация возгорания с применением первичных средств пожаротушения. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">Оценка действий оперативного персонала при возникновении и ликвидации пожара с применением первичных средств пожаротушения. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">5.2. Финал Соревнований проводится в 7 этапов: </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">5.2.1. Этапы №№ 1-6 в соответствии с п. 5.1. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">5.2.2. 7 этап. Оперативное обслуживание и подготовка к производству ремонтных работ на устройствах и комплексах РЗА, находящихся в оперативно-технологическом управлении и диспетчерском управлении.<b> </b></p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">Включает практические действия оперативного персонала при выполнении организационных и технических мероприятий при подготовке и реализации технических воздействий по изменению эксплуатационного состояния устройств и комплексов РЗА. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">5.3. Порядок и правила прохождения этапов, оценки результатов и профессиональных навыков персонала, определяются соответствующими утвержденными положениями о проведении этапов. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p> </p>
 
<p align="center"><b>6. </b><b>Порядок проведения Соревнований. </b></p>
 
<p> </p>
 
<p style="text-align: justify;">
  <br />
</p>

<p style="text-align: justify;">6.1. Каждой команде представляется удаленный или иной доступ к программно-техническим средствам и средствам тренажерной подготовки, применяемым на этапах Соревнований. </p>
 
<p style="text-align: justify;">Соответствующий доступ предоставляется командам не позднее, чем за 30 дней до начала этапов Соревнований.<b> </b></p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;"> 6.2. Для ознакомления с условиями проведения Соревнований участники прибывают на место проведения в соответствии с утвержденной программой проведения Соревнований. </p>
 
<p style="text-align: justify;">6.3. До начала Соревнований мандатная комиссия проверяет команды на соответствие требованиям настоящего Положения и Положения о мандатной комиссии. Жеребьевка стартовых номеров команд проводится в рамках процедуры открытия Соревнований. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">6.4. В рамках прохождения мандатной комиссии каждый участник команды должен представить справку о прохождении медицинского осмотра. Справка должна быть выдана учреждением с лицензией на осуществление медицинской деятельности, содержать печать медицинского учреждения, подпись и печать врача, дату выдачи, ФИО участника. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">6.5. Медицинское сопровождение в период проведения Соревнований обеспечивают организаторы Соревнований, указанные в п. 1.2. настоящего Положения, совместно с ответственными работниками филиалов ПАО «РусГидро» - «Волжская ГЭС», «Загорская ГАЭС», «Саяно-Шушенская ГЭС имени П.С. Непорожнего», в том числе: </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">6.5.1. Дежурство общепрофильной врачебной выездной бригады скорой медицинской помощи или присутствие врача на каждом объекте, где проводятся Соревнования. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">6.5.2. Проведение перед началом Соревнований целевого инструктажа участников по охране здоровья во время проведения Соревнований, контролю нагрузок и восстановлению после участия в Соревнованиях. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">6.5.3. Проведение медицинского осмотра перед началом этапа № 6 «Ликвидация возгорания с применением первичных средств пожаротушения» для участников Соревнований. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">6.6. Порядок организации оказания медицинской помощи, требования к прохождению участниками Соревнований медицинского осмотра для получения допуска к Соревнованиям, порядок проведения медицинского осмотра перед этапом № 6 «Ликвидация возгорания с применением первичных средств пожаротушения», программы целевого инструктажа определяются Регламентом организации медицинского обслуживания участников Соревнований, утверждаемым Оргкомитетом. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">6.7. Прохождение Соревнований командами осуществляется в соответствии с утвержденным графиком движения команд по этапам и результатами жеребьевки. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">6.8. Команды допускаются к прохождению этапа при выполнении условий, установленных положением о проведении соответствующего этапа. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">6.9. Задания на выполнение работ команды получают от членов судейских бригад этапов. Отсчет зачетного времени начинается после того, как судьи этапа дают команду на выполнение задания. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">6.10. В ходе выполнения заданий этапов участники не имеют права делать замечания по действиям членов команд и судейских бригад этапа, обсуждать свои действия с лицами, не принимающими участия в выполнении работ на этапе, или обсуждать действия судейской бригады. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">6.11. По ходу этапа и при принятии решения члены судейских бригад этапа имеют право задавать участникам команд уточняющие вопросы. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">6.12. Руководитель команды вправе присутствовать на этапе, который проходит его команда, и не имеет права вмешиваться в ход проведения этапа, делать замечания, обращаться к судьям и участникам, обсуждать действия судейской бригады. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">6.13. Участникам команд после прохождения этапов запрещается доводить задание этапа до сведения участников других команд. В случае выявления нарушения данного требования, команда по решению Главной судейской комиссии может быть отстранена от участия в Соревнованиях.</p>

<p style="text-align: justify;">
  <br />
</p>
 
<p> </p>
 
<p align="center"><b>7. </b><b>Судейство Соревнований. </b></p>

<p align="center"><b>
    <br />
  </b></p>
 
<p> </p>
 
<p style="text-align: justify;">7.1. Организация и координация судейства Соревнований осуществляется Главной судейской комиссией. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">7.2. Судейские бригады на этапах комплектуются из представителей департаментов исполнительного аппарата ПАО «РусГидро», филиала ПАО «РусГидро» - «КорУнГ», АО «СО ЕЭС» (по согласованию) и других организаций, привлеченных к проведению Соревнований, в исключительных случаях фирм-разработчиков программного обеспечения, используемого на этапе. Как правило, состав судейской бригады на каждом этапе – старший судья этапа и двое судей этапа. Допускается назначение судейской бригады в меньшем количестве по решению Главного судьи. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">7.3. Главная судейская комиссия производит (дополнительно к пункту 4.2. настоящего Положения): </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">- рассмотрение и утверждение дополнительной организационной документации, необходимой для проведения судейства Соревнований; </p>
 
<p style="text-align: justify;">- оперативную корректировку положений о проведении отдельных этапов; </p>
 
<p style="text-align: justify;">- рассмотрение и утверждение Акта мандатной комиссии; </p>
 
<p style="text-align: justify;">- оценку внешнего вида участников Соревнований и соответствие его требованиям настоящего Положения и положений об этапах; </p>
 
<p style="text-align: justify;">- инструктаж судейских бригад на этапах; </p>
 
<p style="text-align: justify;">- разработку и корректировку графика движения команд по этапам Соревнований; </p>
 
<p style="text-align: justify;">- руководство и контроль за ходом Соревнований; </p>
 
<p style="text-align: justify;">- принятие решений о допуске или отстранении участников; </p>
 
<p style="text-align: justify;">- рассмотрение и принятие решений по апелляциям соревнующихся; </p>
 
<p style="text-align: justify;">- рассмотрение и утверждение протоколов, представляемых судейскими бригадами; </p>
 
<p style="text-align: justify;">- подведение итогов Соревнований, утверждение итогового протокола Соревнований. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">7.4. В обязанности судейских бригад на этапах входит: </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">- разработка документации по выполнению этапа; </p>
 
<p style="text-align: justify;">- ознакомление участников с рабочими местами и учебными техническими средствами; </p>
 
<p style="text-align: justify;">- организация допуска участников на рабочие места; </p>
 
<p style="text-align: justify;">- выдача заданий и вводных, предусмотренных условиями решения задач; </p>
 
<p style="text-align: justify;">- организация устранения неисправностей технических средств на этапе; </p>
 
<p style="text-align: justify;">- контроль поведения участников и принятие решений об их снятии с этапа за нарушение условий соревнования; </p>
 
<p style="text-align: justify;">- оценка качества выполнения заданий каждой командой на этапах; </p>
 
<p style="text-align: justify;">- оформление протоколов по итогам прохождения этапов. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p> </p>
 
<p align="center"><b>8. </b><b>Система оценок. </b></p>
 
<p> </p>
 
<p style="text-align: justify;">
  <br />
</p>

<p style="text-align: justify;">8.1. Оценка работ на всех этапах производится по балльной системе, на этапах №№ 1 – 3, 5, как правило, с использованием применяемого на этапе программного обеспечения (машинная оценка) и с учетом требований соответствующего положения о проведении этапа, а на этапах № 4, 6, 7 – членами судейской бригады в соответствии с положениями о проведении этапов. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">8.2. Поощрительные и штрафные баллы при выполнении заданий на этапах устанавливаются соответствующими положениями о проведении этапов Соревнований. </p>
 
<p style="text-align: justify;">
  <br />
</p>

<p style="text-align: justify;">8.3. Протоколы по результатам выполнения заданий на этапах, подписанные старшими судьями и членами судейских бригад, передаются в секретариат Соревнований в течение часа после окончания выполнения задания. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">8.4. Протокол с результатами прохождения этапа командой утверждается Главным судьей (заместителем главного судьи в его отсутствии) в течение часа после поступления в секретариат при условии неподачи апелляции руководителем команды. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p> </p>
 
<p align="center"><b>9. </b><b> Решение спорных вопросов. </b></p>

<p align="center"><b>
    <br />
  </b></p>
 
<p> </p>
 
<p style="text-align: justify;">9.1. Руководитель команды имеет право знакомиться с результатами прохождения своей команды любого этапа и подавать обоснованные письменные апелляции в Главную судейскую комиссию на решения судейских бригад соответствующих этапов в течение одного часа после поступления протокола в секретариат Соревнований. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">9.2. Апелляции рассматриваются Главной судейской комиссией, и принятые решения доводятся до заинтересованных лиц не позднее 3 часов после подачи апелляции. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">9.3. При обоснованном протесте вопрос или задание засчитываются, как выполненные. При отклонении протеста (признание его как необоснованного) участник штрафуется на количество баллов, равных максимальной оценке опротестованного вопроса или задания на этапе. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p><b> </b></p>
 
<p align="center"><b>10. </b><b>Подведение итогов и награждение победителей. </b></p>

<p align="center"><b>
    <br />
  </b></p>
 
<p> </p>
 
<p style="text-align: justify;">10.1. Подведение итогов Соревнований проводится на закрытом заседании Главной судейской комиссии с участием всех старших судей этапов. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">10.2. Победителем Соревнований признается команда, набравшая наибольшее количество баллов по сумме всех этапов. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">Победителями Соревнований в номинациях «Лучший по профессии» признаются участники команд, набравшие наибольшую сумму баллов на всех этапах: </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">- «Лучший начальник смены станции»; </p>
 
<p style="text-align: justify;">- «Лучший начальник смены цеха (машзала)»; </p>
 
<p style="text-align: justify;">- «Лучший машинист гидротурбинного оборудования»; </p>
 
<p style="text-align: justify;">- «Лучший дежурный электромонтёр ГЭС». </p>

