<p align="center"><b>3. </b><b>Требования к участникам Соревнований. </b></p>

<p align="center"><b>
    <br />
  </b></p>
 
<p> </p>
 
<p style="text-align: justify;">3.1. Каждая команда возглавляется начальником Оперативной службы (иное по решению Главного судьи Соревнований на основании письменного обращения руководителя организации, заявляющего команду). </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">Для участия в торжественных мероприятиях открытия и закрытия финала Соревнований по решению председателя Организационного комитета (далее – Оргкомитет) могут быть приглашены руководители организаций, заявивших команды для участия. </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">3.2. К участию в Соревнованиях допускается бригада оперативного персонала ГЭС в следующем составе (указаны минимальные требования к группам по электробезопасности): </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">- начальник смены станции (ГЭС, каскада ГЭС) – V гр. по электробезопасности; </p>
 
<p style="text-align: justify;">- начальник смены цеха (машзала) – IV гр. по электробезопасности; </p>
 
<p style="text-align: justify;">- машинист гидротурбинного оборудования (дежурный электромонтер) – III гр. по электробезопасности; </p>
 
<p style="text-align: justify;">- дежурный электромонтёр ГЭС (начальник смены подстанции, дежурный инженер подстанции) – IV гр. по электробезопасности. </p>
 
<p style="text-align: justify;">
  <br />
</p>

<p style="text-align: justify;">В случае отсутствия какой-либо должности в штатном расписании разрешается включать в состав команды, вместо: </p>

<p style="text-align: justify;">
  <br />
</p>
 
<p style="text-align: justify;">- начальника смены станции (ГЭС, каскада ГЭС) – дежурного инженера; </p>
 
<p style="text-align: justify;">- начальника смены цеха (машзала) – начальника смены станции (ГЭС), дежурного инженера; </p>
 
<p style="text-align: justify;">- машиниста гидротурбинного оборудования (электромонтера) – начальника смены цеха (машзала), начальника смены станции (ГЭС) или дежурного инженера; </p>
 
<p style="text-align: justify;">- дежурного электромонтёра (дежурный инженер подстанции) – начальника смены цеха (машзала), начальника смены станции (ГЭС) или дежурного инженера. </p>
 
<p style="text-align: justify;">
  <br />
</p>

<p style="text-align: justify;">В таких случаях решение о допуске к участию команд принимает Главный судья Соревнований. К заявке на участие, в данном случае, должно быть приложено утвержденное штатное расписание подразделения (с существующей на дату подачи заявки штатной расстановкой пофамильно), в котором находится оперативный персонал объекта. </p>

<p style="text-align: justify;">
  <br />
