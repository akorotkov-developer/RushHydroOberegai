<p align="justify">- состав Главной судейской комиссии,</p>
 
<p align="justify">- сценарий открытия и закрытия Соревнований (приложение 5),</p>
 
<p align="justify">- состав секретариата Соревнований и Положение о секретариате Соревнований (приложение 6).</p>
 
