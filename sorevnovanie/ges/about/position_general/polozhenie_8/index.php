 </p>
 
<p align="justify">1.1.2. <b>По региону «Восток»</b> – на базе Филиала ПАО «РусГидро» – «Саяно-Шушенская ГЭС имени П.С. Непорожнего» и Саяно-Шушенского учебно-производственного информационного центра Филиала ПАО «РусГидро» – «КорУнГ» (Республика Хакасия, р.п. Черемушки) – <b>с 18 по 25 июня 2018 года.</b> </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">В Соревнованиях принимают участие команды оперативного персонала филиалов ПАО «РусГидро» – «Саяно-Шушенская ГЭС имени П.С. Непорожнего», «Новосибирская ГЭС», «Бурейская ГЭС», «Зейская ГЭС», «Воткинская ГЭС», «Камская ГЭС», а также команды подконтрольных организаций – ПАО «Богучанская ГЭС», Филиал ПАО «Колымаэнерго» – «Колымская ГЭС имени Фриштера Ю.И.», Филиал ПАО «Якутскэнерго» – «Каскад Вилюйских ГЭС имени Е.Н. Батенчука».</p>
 
<p align="justify"> 
