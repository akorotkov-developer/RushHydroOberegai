  <br />
 </p>
 
<p align="justify">4.2. Главный судья Соревнований утверждает:</p>
 
<p align="justify">- графики движения команд по этапам (приложение 7),</p>
 
<p align="justify">- составы судейских бригад на этапах,</p>
 
<p align="justify">- составы судейских комиссий региональных Соревнований,</p>
 
<p align="justify">- составы мандатных комиссий Соревнований,</p>
 
<p align="justify">- перечень и состав команд, участвующих в региональных и финальных соревнованиях,</p>
 
<p align="justify">- Положения по этапам,</p>
 
<p align="justify">- Положение о мандатной комиссии,</p>
 
<p align="justify">- итоговые протоколы Соревнований,</p>
 
<p align="justify">- перечень помещений для проведения региональных этапов и финала Соревнований.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">4.3. Судейские бригады на этапах с привлечением разработчиков программных средств определяют перечень заданий, выполняемых участниками Соревнований, разрабатывают положения о проведении этапов, обеспечивают доработку и приемку программных средств, используемых при выполнении заданий на этапах.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">4.4. Положения о проведении этапов Соревнований утверждаются Главным судьей соревнований, и рассылаются участникам не позднее 30 дней до начала Соревнований.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">4.5. Финансирование подготовки и проведения Соревнований осуществляется в соответствии со сметой затрат на подготовку и проведение Соревнований Филиалом ПАО «РусГидро» – «КорУнГ», в том числе за счет взносов участников Соревнований.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">4.6. Вопросы, не регламентируемые настоящим Положением, выносятся на рассмотрение Оргкомитета или Главной судейской комиссии.</p>
 
