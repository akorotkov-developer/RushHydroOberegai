<p align="justify"><b>5. </b><b>Этапы Соревнований.</b></p>
 
<p align="justify"> </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">5.1. <b>Региональные Соревнования</b> оперативного персонала ГЭС проводятся в 6 этапов:</p>
 
<p align="justify"><b> 
    <br />
   </b></p>
 
<p align="justify"><b>1 этап</b>. Проверка знаний нормативно-технических документов (НТД) и проверка умения выявлять отступления от требований НТД с использованием ПЭВМ и специализированного программного обеспечения. </p>
 
<p align="justify"><b> 
    <br />
   </b></p>
 
<p align="justify"><b>2 этап</b>. Производство оперативных переключений.</p>
 
<p align="justify">Включает выполнение на компьютерном тренажере технологических операций по оперативным переключениям в электроустановках. </p>
 
<p align="justify"><b> 
    <br />
   </b></p>
 
<p align="justify"><b>3 этап.</b> Противоаварийная тренировка.</p>
 
<p align="justify">Включает выполнение на компьютерном тренажере технологических операций по управлению электротехническим, гидротурбинным и гидромеханическим оборудованием в нормальных режимах эксплуатации и при возникновении нарушений в работе оборудования гидроэлектростанций. </p>
 
<p align="justify"><b> 
    <br />
   </b></p>
 
<p align="justify"><b>4 этап.</b> Оказание первой помощи пострадавшим на производстве. </p>
 
<p align="justify">Определяется уровень подготовки персонала по оказанию доврачебной помощи пострадавшим при несчастных случаях с использованием специального робота-тренажера «ГОША».</p>
 
<p align="justify"><b> 
    <br />
   </b></p>
 
<p align="justify"><b>5 этап. </b>Ведение энергетического режима работы ГЭС.</p>
 
<p align="justify">Включает выполнение на компьютерном тренажере технологических операций по ведению электрического режима и расчета водного режима.</p>
 
<p align="justify"><b> 
    <br />
   </b></p>
 
<p align="justify"><b>6 этап. </b>Проверка готовности персонала к ликвидации возгорания с применением первичных средств пожаротушения.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">5.2. <b>Финал Соревнований</b> проводится аналогично в 6 этапов, описанных в п. 5.1.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">5.3. Порядок и правила прохождения этапов, оценки результатов и профессиональных навыков персонала, определяются соответствующими утвержденными положениями об этапах.</p>
 
<p align="justify"> </p>
 
<p align="justify"><b> 
    <br />
   </b></p>
 
<p align="justify"><b>6. </b><b>Порядок проведения Соревнований.</b></p>
 
<p align="justify"> </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">6.1. Для изучения нормативно-технической документации, знания которой будут проверяться в ходе Соревнований, каждой команде представляется удаленный или иной доступ к функциональности системы (программного обеспечения), на которой будет проводиться соответствующий этап. </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">Для изучения порядка проведения операций на тренажере оперативных переключений каждой команде предоставляется доступ к функциональности системы (программного обеспечения) с реализованной схемой, используемой для подготовки заданий на соответствующие этапы.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">Соответствующий доступ предоставляется командам-участницам не позднее, чем за 30 дней до начала Соревнований.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">6.2. Для ознакомления с условиями проведения Соревнований участники прибывают на место проведения не менее чем за сутки до их открытия.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">6.3. До начала Соревнований мандатная комиссия проверяет команды на соответствия требованиям настоящего Положения и Положения о мандатной комиссии. Жеребьевка стартовых номеров команд проводится в период процедуры открытия Соревнований.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">6.4. Процедура торжественного открытия и закрытия соревнования определяется сценарием.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">6.5. В открытии Соревнований принимают участие Председатель Оргкомитета (или его заместитель) и Главный судья соревнований.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">6.6. Прохождение Соревнований командами осуществляется в соответствии с утвержденным графиком и результатами жеребьевки.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">6.7. Команды допускаются к прохождению этапа при выполнении условий, установленных положением по этапу.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">6.8. Задания на выполнение работ команды получают от судей на этапах. Отсчет зачетного времени начинается после того, как судьи на этапе дают команду на выполнение задания. </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">6.7. В ходе выполнения заданий этапов участники не имеют права делать замечания по действиям членов команд и судей, обсуждать свои действия с лицами, не принимающими участия в выполнении работ на этапе, или обсуждать действия судейской бригады. </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">6.8. По ходу этапа и при принятии решения члены судейских бригад на этапе имеют право задавать участникам команд уточняющие вопросы. </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">6.9. Руководитель команды вправе присутствовать на этапе, который проходит его команда и не имеет права вмешиваться в ход проведения этапа, делать замечания, обращаться к судьям и участникам, обсуждать действия судейской бригады.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">6.10. Участникам команд после прохождения этапов запрещается доводить задание этапа до сведения участников других команд. В случае выявления нарушения данного требования, команда по решению Главной судейской комиссии может быть снята с Соревнований.</p>
 
<p align="justify"> </p>
 
<p align="justify"><b> 
    <br />
   </b></p>
 
<p align="justify"><b>7. </b><b>Судейство Соревнований.</b></p>
 
<p align="justify"> </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">7.1. Организация и координация судейства Соревнований осуществляется Главной судейской комиссией.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">7.2. Судейские бригады на этапах комплектуются из представителей департаментов Исполнительного аппарата ПАО «РусГидро», Филиала ПАО «РусГидро» - «КорУнГ», АО «СО ЕЭС» (по согласованию) и других организаций привлеченных к проведению Соревнований, в исключительных случаях фирм-разработчиков программного обеспечения, используемого на этапе. Как правило, состав судейской бригады на каждом этапе – старший судья этапа и двое судей этапа. Допускается назначение судейской бригады в меньшем количестве по решению Главного судьи соревнований.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">7.3. Главная судейская комиссия производит (дополнительно к пункту 4.2. настоящего Положения): </p>
 
<p align="justify">- рассмотрение и утверждение дополнительной организационной документации, необходимой для проведения судейства соревнования; </p>
 
<p align="justify">- оперативную корректировку положений о проведении отдельных этапов;</p>
 
<p align="justify">- рассмотрение и утверждение Акта мандатной комиссии; </p>
 
<p align="justify">- оценку внешнего вида участников Соревнований и соответствие его требованиям настоящего Положения и положений об этапах; </p>
 
<p align="justify">- инструктаж судейских бригад на этапах; </p>
 
<p align="justify">- разработку и корректировку графика выполнения этапов Соревнований;</p>
 
<p align="justify">- руководство и контроль за ходом Соревнований; </p>
 
<p align="justify">- принятие решений о допуске или отстранении участников; </p>
 
<p align="justify">- рассмотрение и принятие решений по апелляциям соревнующихся; </p>
 
<p align="justify">- рассмотрение и утверждение протоколов, представляемых судейскими бригадами; </p>
 
<p align="justify">- подведение итогов Соревнований, утверждение итогового протокола Соревнований.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">7.4. В обязанности судейских бригад на этапах входит: </p>
 
<p align="justify">- разработка документации по выполнению этапа;</p>
 
<p align="justify">- ознакомление участников с рабочими местами и учебными техническими средствами; </p>
 
<p align="justify">- организация допуска участников на рабочие места; </p>
 
<p align="justify">- выдача заданий и вводных, предусмотренных условиями решения задач; </p>
 
<p align="justify">- организация устранения неисправностей технических средств на этапе; </p>
 
<p align="justify">- контроль поведения участников и принятие решений об их снятии с этапа за нарушение условий соревнования; </p>
 
<p align="justify">- оценка качества выполнения заданий каждой командой на этапах; </p>
 
<p align="justify">- оформление протоколов по итогам прохождения этапов. </p>
 
<p align="justify"> </p>
 
<p align="justify"><b> 
    <br />
   </b></p>
 
<p align="justify"><b>8. </b><b>Система оценок.</b></p>
 
<p align="justify"> </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">8.1. Оценка работ на всех этапах производится по бальной системе, на этапах №№ 1 – 3, 5, как правило, с использованием применяемого на этапе программного обеспечения (машинная оценка) и с учетом требований положения по этапам, а на этапе № 4 и 6 – членами судейской бригады в соответствии с положениями об этапах.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">8.2. Поощрительные и штрафные баллы при выполнении заданий на этапах устанавливаются соответствующими положениями о проведении этапов Соревнований.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">8.3. Протоколы по результатам выполнения заданий на этапах, подписанные старшими судьями и членами судейских бригад, передаются в секретариат Соревнований в течение часа после окончания выполнения задания.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">8.4. Протокол с результатами прохождения этапа командой утверждается главным судьей (заместителем главного судьи в его отсутствии) в течение часа после поступления в секретариат при условии неподачи апелляции руководителем команды.</p>
 
<p align="justify"> </p>
 
<p align="justify"><b> 
    <br />
   </b></p>
 
<p align="justify"><b>9. </b><b> Решение спорных вопросов.</b></p>
 
<p align="justify"> </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">9.1. Руководитель команды имеет право знакомиться с результатами прохождения своей команды любого этапа и подавать обоснованные письменные апелляции в Главную судейскую комиссию на решения судейских бригад соответствующих этапов в течение одного часа после поступления протокола в секретариат Соревнований.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">9.2. Апелляции рассматриваются Главной судейской комиссией, принятые решения доводятся до заинтересованных лиц не позднее 3 часов после подачи апелляции. </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">9.3. При обоснованном протесте вопрос или задание засчитываются, как выполненные. При отклонении протеста (признание его как необоснованного) участник штрафуется на количество баллов, равных максимальной оценке опротестованного вопроса или задания на этапе.</p>
 
<p align="justify"><b> </b></p>
 
<p align="justify"><b> 
    <br />
   </b></p>
 
<p align="justify"><b>10. </b><b>Подведение итогов и награждение победителей.</b></p>
 
<p align="justify"> </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">10.1. Подведение итогов Соревнований проводится на закрытом заседании Главной судейской комиссии с участием всех старших судей этапов.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">10.2. Победителем Соревнований признается команда, набравшая наибольшее количество баллов по сумме всех этапов. Победителями Соревнований в отдельных номинациях признаются участники команд, набравшие наибольшую сумму баллов на всех этапах. Они объявляются в следующих номинациях:</p>
 
<p align="justify">- «Лучший начальник смены станции»;</p>
 
<p align="justify">- «Лучший начальник смены цеха (машзала)»;</p>
 
<p align="justify">- «Лучший машинист гидротурбинного оборудования»;</p>
 
<p align="justify">- «Лучший дежурный электромонтёр ГЭС».</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">10.3. В случае, если две и более команд набрали одинаковое количество баллов, более высокое место присваивается команде, показавшей лучший результат по сумме этапов № 2 и № 3.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">10.4.Итоговым документом Соревнований является протокол Главной судейской комиссии (судейской комиссии), утвержденный Главным судьей соревнований (заместителем Главного судьи при его отсутствии).</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">10.5. Команды, занявшие в соревнованиях первое, второе и третье места награждаются Почетными дипломами, участники этих команд награждаются Почетными грамотами и ценными призами. Почетными грамотами и ценными призами так же награждаются победители в отдельных номинациях «Лучший по профессии».</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">10.6. Командам, не занявшим призовых мест, вручаются вымпела с символикой Соревнований, дипломы об участии в соревнованиях, памятные сувениры.</p>
 
