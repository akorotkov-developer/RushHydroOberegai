<p align="justify"><b>3. </b><b>Требования к участникам Соревнований.</b></p>
 
<p align="justify"> </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">3.1. Каждая команда возглавляется начальником Оперативной службы (иное по решению Главного судьи соревнований на основании письменного обращения руководителя организации, заявляющего команду).</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">Для участия в торжественных мероприятиях открытия и закрытия финала Соревнований по решению Председателя Организационного комитета соревнований (далее – Оргкомитет) могут быть приглашены руководители организаций, заявивших команды для участия.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">3.2. К участию в Соревнованиях допускается бригада оперативного персонала ГЭС в следующем составе (указаны минимальные требования к группам по электробезопасности):</p>
 
<p align="justify">- начальник смены станции (ГЭС, каскада ГЭС) – V гр. по электробезопасности;</p>
 
<p align="justify">- начальник смены цеха (машзала) – IV гр. по электробезопасности;</p>
 
<p align="justify">- машинист гидротурбинного оборудования (дежурный электромонтер) – III гр. по электробезопасности;</p>
 
<p align="justify">- дежурный электромонтёр ГЭС (начальник смены подстанции, дежурный инженер подстанции) – IV гр. по электробезопасности.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">В случае отсутствия какой-либо должности в штатном расписании разрешается включать в состав команды, вместо:</p>
 
<p align="justify">- начальника смены станции (ГЭС, каскада ГЭС) – дежурного инженера;</p>
 
<p align="justify">- начальника смены цеха (машзала) – начальника смены станции (ГЭС), дежурного инженера;</p>
 
<p align="justify">- машиниста гидротурбинного оборудования (электромонтера) – начальника смены цеха (машзала), начальника смены станции (ГЭС) или дежурного инженера;</p>
 
<p align="justify">- дежурного электромонтёра (дежурный инженер подстанции) – начальника смены цеха (машзала), начальника смены станции (ГЭС) или дежурного инженера.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">В таких случаях решение о допуске к участию команд принимает Главный судья соревнований. К заявке на участие, в данном случае, должно быть приложено утвержденное штатное расписание подразделения (с существующей на дату подачи заявки штатной расстановкой по-фамильно), в котором находится оперативный персонал объекта. </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">Команды участников, как правило, должны формироваться из числа победителей последних проведенных Соревнований профессионального мастерства оперативного персонала на предприятии. Допускается включать в состав команды не более одного участника предыдущих Всероссийских соревнований, при условии изменения его должности в составе бригады. Персонал, участвовавший в соревнованиях до 2012 года включительно, может быть включен в состав команд без ограничений.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">3.3. Заявки на участие в региональных Соревнованиях подаются в письменной форме на Главного судью соревнований до 01 марта 2018 года, в финале Соревнований – до 01 июля 2018 года с указанием наименования предприятия, Ф.И.О. участников команды (полностью), года рождения, должности, группы по электробезопасности, стажа работы в должности, размера одежды (футболка) (по форме приложения 1). </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">3.4. Участники Соревнований должны иметь навыки работы на персональном компьютере (ПК), необходимые для выполнения заданий на этапах, иметь при себе удостоверения о проверке знаний, полис обязательного медицинского страхования и другие документы в соответствии с Положением о мандатной комиссии (приложение 2).</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">3.5. Любая используемая одежда и обувь участников бригады оперативного персонала на этапах Соревнований должна быть единого образца с наличием эмблемы и/или названия предприятия. </p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">3.6. Замена участников соревнования по представлению руководителя команды может быть разрешена Главным судьей до начала Соревнований. Если требуется экстренная замена участника команды (болезнь или иное событие не позволяющее продолжить участие в соревнованиях), то, по согласованию с Главным судьей соревнований, производится замена участника на резервного (если такое возможно оперативно без нарушения графика прохождения этапов Соревнований командой) или замена участника руководителем команды.</p>
 
<p align="justify"> 
  <br />
 </p>
 
<p align="justify">Замена участника работником другого предприятия или другой организации не допускается.</p>
 
