 </p>
 
<p align="justify">4.1. В целях организации и проведения Соревнований Председатель Оргкомитета Соревнований утверждает:</p>
 
<p align="justify">- настоящее Положение,</p>
 
<p align="justify">- перечень наградной и сувенирной продукции с символикой Соревнований,</p>
 
<p align="justify">- программу организационно-технических мероприятий по подготовке Соревнований с назначением ответственных из числа Организационного комитета и сотрудников предприятий, на базе которых проводятся соревнования (по форме приложения 3),</p>
 
