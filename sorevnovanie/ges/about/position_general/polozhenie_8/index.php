  <br />
 </p>
 
<p align="justify">В Соревнованиях принимают участие команды оперативного персонала филиалов ПАО «РусГидро» – «Волжская ГЭС», «Саратовская ГЭС», «Жигулевская ГЭС», «Чебоксарская ГЭС», «Нижегородская ГЭС», «Каскад Верхневолжских ГЭС», «Загорская ГАЭС», «Каскад Кубанских ГЭС», «Дагестанский филиал», «Кабардино-Балкарский филиал», «Карачаево-Черкесский филиал», «Северо-Осетинский филиал» и команда ООО «Башкирская Генерирующая Компания».</p>
 
