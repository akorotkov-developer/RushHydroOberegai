<p align="center"><b>1. </b><b>Общие Положения.</b></p>
 
<p align="center"><b> 
    <br />
   </b></p>
 
<p>Ликвидация нарушений нормального режима работы электростанции осуществляется путём управления технологическими режимами работы и эксплуатационным состоянием оборудования (устройств) направленного на:</p>
 
<ul> 
  <li>устранение опасности для обслуживающего персонала и оборудования, не затронутого технологическим нарушением;</li>
 
  <li> предотвращение развития технологического нарушения;</li>
 
  <li> восстановление в кратчайший срок питания потребителей и качества электроэнергии (частоты и напряжения);</li>
 
  <li> создание наиболее надежной послеаварийной схемы;</li>
 
  <li> выяснение состояния отключившегося во время технологического нарушения оборудования и возможности включения его в работу.</li>
 </ul>
 
<p>На этапе команда производит ликвидацию технологического нарушения в электроустановках и/или на гидротурбинном оборудовании по разработанному сценарию. </p>
 
<p>Действия по ликвидации производятся на ПЭВМ с использованием тренажера &laquo;Модус&raquo;.</p>
 
<p> 
  <br />
 </p>
 
<p><b> </b></p>
 
<p align="center"><b>2. </b><b>Цель этапа.</b></p>
 
<p align="center"><b> 
    <br />
   </b></p>
 
<p>Проверка совместной командной работы оперативного персонала гидроэлектростанции в аварийных ситуациях в условиях ограниченного времени, оценка профессиональных качеств и способности самостоятельно и на основе коллективных действий персонала смены предотвратить развитие и оперативно ликвидировать аварийную ситуацию и обеспечить устойчивую работу оборудования согласно требований Правил предотвращения развития и ликвидации нарушений нормального режима электрической части энергосистем, Инструкции по переключениям в электроустановках, ПТБ, ПТЭ, ПУЭ и других директивных документов.</p>
 
<p> 
  <br />
 </p>
 
<p> </p>
 
