<p align="center"><b><i>Общие правила:</i></b></p>
 
<p align="center"><b><i> 
      <br />
     </i></b></p>
 
<p><i>1. Общее руководство ликвидацией технологического нарушения осуществляет НСС.</i></p>
 
<p><i>2. Переключения на КРУЭ/ОРУ и силовых трансформаторах проводит НСС и ДЭМ.</i></p>
 
<p><i>3. Переключения в схеме гидроагрегата и собственных нужд проводит МГ и НСМ.</i></p>
 
<p><i>4. Переключения ведутся с соблюдением требований к ведению оперативных переговоров в соответствии с &laquo;Типовой инструкцией о порядке ведения оперативных переговоров диспетчерским персоналом диспетчерского центра&raquo;.</i></p>
 
<p><i>5. Действия по ведению оперативных журналов и другой документации на рабочих местах описываются участниками при выполнении задания, рекомендуемым является ведение черновика. Получение и описание оформления диспетчерского разрешения (команды) на производство переключений с фиксацией времени обязательно. </i></p>
 
<p><i>6. Переключения проводятся в соответствии с &laquo;Инструкцией по переключениям&raquo; СО 153-34.20.505-2003 и стандартом СТО 59012820.29.020.005-2011 &laquo;Правила переключений в электроустановках&raquo;.</i></p>
 
<p><i>7. Перед операциями с разъединителями они должны быть осмотрены на отсутствие дефектов и повреждений.</i></p>
 
<p><i>8. Переключения в цепях противоаварийной автоматики и защит производятся в объеме, предусмотренном схемой и тренажером &laquo;Модус&raquo;. В случае необходимости участник может довести до сведения судей необходимые на его взгляд действия в цепях РЗА и ПА, которые не реализованы.</i></p>
 
<p><i>9. Запрещающие плакаты вывешиваются по ходу переключений, непосредственно после проведения операций согласно требований ПОТЭУ (Правил по охране труда при эксплуатации электроустановок).</i></p>
 
<p><i>10. Ликвидация технологического нарушения завершается выводом в ремонт поврежденного оборудования, с подготовкой рабочих мест в части вывешивания плакатов для запрещения действий с коммутационными аппаратами, при ошибочном включении которых может быть подано напряжение на место работы, и заземленное оборудование. </i></p>
 
