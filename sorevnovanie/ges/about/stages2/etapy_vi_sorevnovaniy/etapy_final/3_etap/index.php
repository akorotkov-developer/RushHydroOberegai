  <br />
 </p>
 
<p> 
  <br />
 </p>
 
<p>Судьи проверяют наличие у каждого участника квалификационного удостоверения и соответствие участников протоколу Мандатной комиссии, и заносят данные участников в протокол. При отсутствии квалификационного удостоверения команда допускается к прохождению этапа в полном составе, но при этом команда получает 10 штрафных баллов за каждое отсутствующее удостоверение.</p>
 
<p> 
  <br />
 </p>
 
<p>Участники этапа соревнований с разрешения старшего судьи занимают места за ПЭВМ.</p>
 
<p> 
  <br />
