<p align="center"> </p>
 
<p align="center"><b>5. Система оценок.</b></p>

<p align="center"><b>
    <br />
  </b></p>
 
<p> </p>
 
<p>5.1. Ответ на вопрос признается правильным, если соревнующийся указал все предложенные правильные варианты ответов на вопрос.</p>
 
<p>Ответ на вопрос признается неправильным, если соревнующийся не указал все предложенные правильные варианты ответов, указал неверный ответ или не ответил на вопрос.</p>
 
<p>За правильный ответ начисляется 2 балла; за неправильный ответ или вопрос, оставшийся без ответа, баллы не начисляются.</p>
 
<p>При правильном ответе на все 50 вопросов, участник получает дополнительные баллы - 1 балл за каждую полную минуту сокращенного времени, выделенного на прохождение этапа, но не более 10 бонусных баллов.</p>
 
<p>При правильном ответе менее чем на 40 вопросов любым из участников команды, участник штрафуется на 2 балла за каждый неправильный ответ, недостающий до 40 правильных ответов, или оставленный без ответа вопрос.</p>
 
<p>5.2. Максимально команде по этапу может быть начислено 440 баллов (400 баллов за все правильные ответы всех участников команды (по 2 балла за каждый правильный ответ), и максимально 40 бонусных баллов.</p>
 
<p>5.3. По окончании 50 минут программа заканчивает работу независимо от того, на сколько вопросов участник успел ответить.</p>
 
<p>5.4. Время, затраченное на прохождение этапа, тексты вопросов со всеми вариантами ответов, ответы, указанные участником соревнования и набранное количество баллов отражаются в рабочем протоколе автоматически, в соответствии с программой, по окончании работы участника соревнования. Бонусные баллы определяются судейской бригадой на основании протоколов. Протоколы по окончании прохождения командой этапа по решению судейской бригады могут быть переданы команде.</p>

<p>
  <br />
</p>
 
