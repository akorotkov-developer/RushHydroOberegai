<p align="center"><b>2. Требования к участникам этапа.</b></p>

<p align="center">
  <br />
</p>
 
<p> </p>
 
<p>2.1. Проверка знаний НТД проводится раздельно для каждого участника команды.</p>
 
<p>Участники соревнований должны иметь навыки работы на персональном компьютере (ПЭВМ), необходимые для выполнения задания на этапе.</p>
 
<p>2.2. Соревнующиеся должны иметь при себе удостоверение о проверке знаний с отметками о проверках знаний, индивидуальную инструкцию по оказанию первой помощи при несчастных случаях на производстве, на одежде (единого образца для комплексной бригады ГЭС/ГАЭС) должна быть эмблема предприятия.</p>

<p>
  <br />
</p>
 
<p> </p>
 
<p align="center"><b>3. Состав заданий.</b></p>

<p align="center"><b>
    <br />
  </b></p>
 
<p> </p>
 
<p>3.l. Ha 1-ом этапе соревнований проводится проверка знаний нормативно-технической документации (НТД), с использованием ПЭВМ.</p>
 
<p>Определяется уровень знаний действующих НТД по следующим направлениям:</p>
 
<p>- техническая эксплуатация;</p>
 
<p>- охрана труда;</p>
 
<p>- пожарная безопасность;</p>
 
<p>- промышленная безопасность,</p>
 
<p>3.2. Проверка проводится по четырем программам, соответствующим должностям участников команды. Каждый тест содержит 50 вопросов, составленных на основании положений нормативно-технических документов.</p>
 
<p>3.3. Проверка знаний заключается в выборе правильных ответов на 50 вопросов, сформированных ИС случайным образом из базы вопросов программы.</p>
 
<p>Распределение вопросов по темам для каждого соревнующегося следующее:</p>
 
<p>- для начальника смены станции (ГЭС/ГАЭС), начальник смены цеха (машзала);</p>
 
<ul>
  <li>ПТЭ и НТД - 25 вопросов;</li>

  <li> ОТ - 15 вопросов;</li>

  <li> ППБ - 5 вопросов;</li>

  <li> Промышленная безопасность и другие специальные правила - 5 вопросов;</li>
</ul>
 
<p>Всего: 50 вопросов.</p>
 
<p>- для машиниста гидротурбинного оборудования (электромонтер), дежурный электромонтер (дежурный инженер подстанции):</p>
 
<ul>
  <li>ПТЭ и НТД - 15 вопросов;</li>

  <li> ОТ - 25 вопросов;</li>

  <li> ППБ - 5 вопросов;</li>

  <li> Промышленная безопасность и другие специальные правила - 5 вопросов;</li>
</ul>
 
<p>Всего: 50 вопросов.</p>

<p>
  <br />
</p>
 
<p> </p>
 
<p align="center"><b>4. Порядок проведения этапа.</b></p>

<p align="center">
  <br />
</p>
 
<p> </p>
 
<p>4.1. Для проведения 1-го этапа организуется 4 рабочих места для соревнующихся, одно рабочее место для судейской бригады, которые оборудуются ПЭВМ, объединенных в локальную сеть, и принтером.</p>
 
<p>4.2. Каждой команде для тренировки предоставляется право ознакомления с рабочими местами на этапе согласно программы проведения соревнований и графика ознакомления команд с рабочими местами на этапах. Каждой команде отводится не более 20 минут на ознакомление с рабочими местами этапа.</p>
 
<p>4.3. Тестовые программы для проведения этапа устанавливаются на ПЭВМ судейской бригадой перед началом проведения 1-го этапа. В соответствии с графиком проведения соревнований участники прибывают всей командой к указанному времени, в указанное место и проходят этап одновременно.</p>
 
<p>4.4. Судьи перед началом проведения этапа проверяют у каждого участника удостоверение о проверке знаний, наличие индивидуальной инструкции по оказанию первой помощи при несчастных случаях на производстве и заносят необходимые данные в протокол. При отсутствии удостоверения о проверке знаний и/или инструкции по оказанию первой помощи при несчастных случаях на производстве участник к прохождению этапа не допускается с занесением в протокол оценки 0 баллов.</p>
 
<p>Допущенному к прохождению этапа участнику соревнования проводится краткий инструктаж, и предлагается ответить на 50 вопросов.</p>
 
<p>4.5. Участнику соревнования предоставляется возможность отвечать на вопросы теста в удобной для него последовательности, с возможностью сохранения пропущенных вопросов.</p>
 
<p>4.6. Оценка правильности ответов производится ИС по каждому вопросу НТД. После ответа на все 50 вопросов или окончания контрольного времени, ИС выставляет оценку в баллах с выводом на экран протокола по данному участнику соревнований.</p>
 
<p>Общее время прохождения этапа - 80 минут:</p>
 
<p>- инструктаж судей и проверка удостоверений &ndash; 10 минут;</p>
 
<p>- на ответы каждому участнику отводится - 50 минут;</p>
 
<p>- оформление Протокола этапа &ndash; 20 минут. </p>
 
<p>4.7. Руководитель комплексной бригады команды-участницы Соревнований имеет право присутствовать в качестве наблюдателя при проведении этапа, но при этом любые его переговоры с членами бригады или судьями запрещаются. За несоблюдение данного требования судейская бригада вправе удалить руководителя команды из помещения, где проводится этап, а команде начислить 10 штрафных баллов. Судейская бригада вправе разрешить присутствовать в период проведения этапа в помещении сопровождающим команду лицам и почетным гостям от предприятия, заявившего команду.</p>

