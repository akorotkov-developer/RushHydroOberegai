<p align="center"><b>1. Общие положения.</b></p>
 
<p align="center"> </p>
 
<p>1.1. Цель этапа - проверка знаний нормативно-технических документов и теоретической подготовки команды персонала ГЭС/ГАЭС в составе:</p>
 
<p>- начальник смены станции (ГЭС/ГАЭС);</p>
 
<p>- начальник смены цеха (машзала);</p>
 
<p>- машинист гидротурбинного оборудования (электромонтер);</p>
 
<p>- дежурный электромонтер (дежурный инженер подстанции).</p>
 
<p> </p>
 
<p>1.2. Проверка знаний требований нормативно-технических документов проводится на базе обучающе-контролирующей системе &laquo;ОЛИМПОКС&raquo; (далее- ИС) с использованием тестовых программ, составленных на базе действующих НТД (Приложение 1).</p>
 
<p>1.3. Для подготовки к проведению соревнований на сервере филиала ОАО &quot;РусГидро&quot; - &quot;КорУнГ&quot; по адресу http://node9020.olimpoks.ru организован доступ к ИС. 
  <br />
</p>

<p>
  <br />
