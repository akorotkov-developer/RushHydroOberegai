<p align="center"><b>6. Судейство этапа.</b></p>

<p align="center"><b>
    <br />
  </b></p>
 
<p> </p>
 
<p>6.1. Для оценки действий соревнующихся назначается судейская бригада. &ndash; старший судья этапа и судья этапа. Допускается назначение судейской бригады в ином составе по решению главного судьи.</p>
 
<p>6.2. На рабочем месте судейской бригады этапа должны быть: </p>
 
<p>- телефоны для связи с Главной судейской комиссией и секретариатом;</p>
 
<p>- полный комплект нормативной документации в соответствии с &quot;Перечнем нормативно-технических документов, законодательных актов, по которым проводится проверка знаний персонала ГЭС&quot; (приложение 1);</p>
 
<p>- список участников соревнований;</p>
 
<p>- бланки итоговых протоколов оценки знаний (приложение 2);</p>
 
<p>- график прохождения этапов;</p>
 
<p>- Положение о проведении соревнований и Положение о проведении 1-го этапа соревнований.</p>
 
<p>6.3. Количество баллов, набранное участниками команды на этапе, заносится в итоговый протокол, который подписывается всеми судьями этапа; и не позднее одного часа после окончания этапа передаётся в секретариат соревнований. В случае отсутствия у руководителя команды претензий к результатам этапа, на протоколе им дополнительно делается запись &laquo;замечаний нет&raquo;.</p>
 
<p> </p>
 
<p>6.4. При определении первенства по профессиям, в случае равенства баллов у двух и более участников соревнований, предпочтение отдаётся тому, кто затратил на прохождение этапа меньшее количество времени.</p>
 
<p>6.5. Окончательная оценка по этапу принимается судейской бригадой.</p>

