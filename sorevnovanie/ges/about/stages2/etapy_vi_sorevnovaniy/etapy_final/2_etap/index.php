<p align="center"><b><i>Общие правила:</i></b></p>
 
<p><i>1. Общее руководство переключениями осуществляет НСС.</i></p>
 
<p><i>2. В переключения</i><i>x</i><i> на КРУЭ/ОРУ и силовых трансформаторах участвуют НСС и ДЭМ, контролирующим лицом в части подготовки бланка переключений является НСМ (работа с бланком, получение задания от НСС и т.п.).</i></p>
 
<p><i>3. Переключения в схеме гидроагрегата и собственных нужд проводит МГ и НСМ.</i></p>
 
<p><i>4. Переключения проводятся по бланкам переключений, использование бланков условно (на этапе оформляются бланки переключений (ДЭМ и МГ в зоне своей ответственности) в соответствии с установленными требованиями с отражением в нем не менее десяти операций, включающих в себя не менее двух операций в цепях РЗА). </i></p>
 
<p><i>5. Переключения ведутся с соблюдением требований к ведению оперативных переговоров в соответствии с &laquo;Типовой инструкцией о порядке ведения оперативных переговоров диспетчерским персоналом диспетчерского центра&raquo;.</i></p>
 
<p><i>6. Действия по ведению оперативных журналов и другой документации на рабочих местах описываются участниками при выполнении задания, рекомендуемым является ведение черновика. Получение и описание оформления диспетчерского разрешения (команды) на производство переключений с фиксацией времени обязательно. </i></p>
 
<p><i>7. Переключения проводятся в соответствии с &laquo;Инструкцией по переключениям&raquo; СО 153-34.20.505-2003 и стандартом СТО 59012820.29.020.005-2011 &laquo;Правила переключений в электроустановках&raquo;.</i></p>
 
<p><i>8. Перед операциями с разъединителями они должны быть осмотрены на отсутствие дефектов и повреждений.</i></p>
 
<p><i>9. Переключения в цепях противоаварийной автоматики и защит производятся в объеме, предусмотренном схемой и 

 тренажером &laquo;Модус&raquo;. В случае необходимости участник может довести до сведения судей необходимые на его взгляд действия в цепях РЗА и ПА, которые не реализованы </i><i>тренажером &laquo;Модус&raquo;</i><i>.</i></p>
 
<p><i>10. Запрещающие плакаты вывешиваются по ходу переключений, непосредственно после проведения операции согласно требований ПОТЭУ (Правил по охране труда при эксплуатации электроустановок).</i></p>
 
<p><i>11. Оформление окончания переключений осуществляется в установленном порядке, обязательна подготовка рабочих мест в части вывешивания плакатов для запрещения действий с коммутационными аппаратами, при ошибочном включении которых может быть подано напряжение на место работы, и заземленное оборудование. </i></p>
 
<p><i>12. После запуска тренировки членам команды запрещается обращаться к другим членам команды и третьим лицам за советами по вопросам, которые они должны решать самостоятельно, за нарушение данного запрета судьи на этапе могут снять с команды 5 баллов за каждое нарушение.</i></p>
 
<p><i>13. Членам команды разрешено обращаться к специалистам организации-разработчика программного обеспечения по вопросам, связанным с работой на ПЭВМ и с &laquo;Модус&raquo;.</i></p>
 
<p><i>14. Тип оборудования, используемого в схеме, не оговоренный заданием или судьями, участники вправе принимать аналогичным установленному на обслуживаемом ими объекте (Информация о подобных допущениях доводится до судей на инструктаже или по ходу выполнения задания перед выполнением соответствующих операций, определенных принятыми особенностями оборудования).</i></p>
 
