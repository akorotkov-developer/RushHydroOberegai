<p align="center"><b>1. Общие положения.</b></p>
 
<p>На этапе команда производит оперативные переключения в электроустановках и необходимые операции с гидротурбинным и гидромеханическим оборудованием. </p>
 
<p>Переключения производятся на ПЭВМ с использованием тренажера &laquo;Модус&raquo;. 
  <br />
 </p>
 
<p> 
  <br />
 </p>
 
<p><b> </b></p>
 
<p align="center"><b>2. Цель этапа.</b></p>
 
<p>­ оценка профессиональных знаний и навыков ведения оперативных переключений в электроустановках;</p>
 
<p>­ выявление типовых ошибок и отступлений от требований действующих СТО 59012820.29.020.005-2011 &laquo;Правила переключений в электроустановках&raquo;, Инструкции по переключениям в электроустановках, ПТЭ, ПТБ и других директивных документов.</p>
 
<p> 
  <br />
 </p>
 
<p><b> </b></p>
 
<p align="center"><b>3. Состав и требования к участникам.</b></p>
 
<p>В соревнованиях принимают участие команды в составе:</p>
 
