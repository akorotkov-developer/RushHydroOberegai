<p>В соответствии с графиком проведения соревнований участники прибывают на этап всей командой к указанному времени.По прибытии команды на этап НСС докладывает старшему судье этапа о готовности команды к выполнению задания. 
  <br />
 </p>
 
<p> 
  <br />
 </p>
 
<p>Судьи проверяют наличие у каждого участника квалификационного удостоверения и соответствие участников протоколу Мандатной комиссии, заносят данные участников в протокол. При отсутствии квалификационного удостоверения команда допускается к прохождению этапа в полном составе, но при этом команда получает 10 штрафных баллов за каждое отсутствующее удостоверение. 
  <br />
 </p>
 
<p> 
  <br />
 </p>
 
<p>Участники этапа соревнований с разрешения старшего судьи занимают места за ПЭВМ.</p>
 
<p> 
  <br />
 </p>
 
<p> </p>
 
<p align="center"><b>4. Порядок проведения этапа.</b></p>
 
<p>Для проведения этапа оборудуются 5 рабочих мест ПЭВМ с необходимым ПО &ndash; 4 рабочих места для участников соревнований (каждое рабочее место - под определенную должность) и рабочее место судей. ПЭВМ подключены к общему (сетевому) принтеру, размещенному на этапе.</p>
 
<p> 
  <br />
 </p>
 
<p>Оперативные переговоры между участниками команды осуществляются посредством телефонной связи, установленной на каждом рабочем месте участника и судьи соревнований, выполняющим роль дежурного диспетчера/главного инженера.</p>
 
<p> 
  <br />
 </p>
 
<p> </p>
 
<p>В помещении, при прохождении командой этапа, имеют право присутствовать:</p>
 
<ul> 
  <li>руководитель команды;</li>
 
  <li> сопровождающие команду лица и почетные гости от предприятия, заявившего команду (с разрешения старшего судьи этапа).</li>
 </ul>
 
<p> 
  <br />
 </p>
 
<p>Судейство этапа осуществляется бригадой судей из двух или трех человек возглавляемой старшим судьей этапа и сопровождается технологической поддержкой специалистов организаций-разработчиков программного обеспечения, которые могут быть привлечены в качестве судей этапа. 
  <br />
 </p>
 
<p> 
  <br />
 </p>
 
<p>Перед началом прохождения командой этапа судейская бригада рассаживает участников на закрепленные за конкретными должностями рабочие места, и проводит команде инструктаж в объеме:</p>
 
<ul> 
  <li>нормальной схемы электрических соединений ГЭС;</li>
 
  <li> кратких сведений по составу оборудования;</li>
 
  <li> исходного режима работы ГЭС;</li>
 
  <li> состояния оперативной схемы. 
    <br />
   </li>
 </ul>
 
<p> 
  <br />
 </p>
 
<p>Участники команды при получении инструктажа могут задать дополнительные вопросы судейской бригаде, касающиеся компоновки схемы, типа оборудования, зон обслуживания, режима энергосистемы и т.п., ответы на которые доводятся до участников команды на усмотрение судейской бригады. Общая продолжительность инструктажа не более 10 минут. При проведении инструктажа НСС обязан довести до сведения судейской бригады информацию о принятых командой допущениях, каков порядок подготовки переключений и т.п. (в части того оборудования о котором в задании на этап однозначно не дано таких определений).</p>
 
<p> 
  <br />
 </p>
 
<p>После получения инструктажа, НСС определяет готовность команды, и сообщает об этом судьям. Работа c тренажерным комплексом на ПЭВМ производится в режиме контрольной тренировки по проведению оперативных переключений.</p>
 
<p> 
  <br />
 </p>
 
<p>Производство оперативных переключений начинается после запуска тренировки на ПЭВМ специалистом организации-разработчика программного обеспечения. С этого времени начинается отсчет времени на выполнение тренировки.</p>
 
<p> 
  <br />
 </p>
 
<p>Во время производства оперативных переключений судьи на этапе выполняют по отношению к команде обязанности вышестоящего диспетчерского, административно-технического персонала, персонала иных организаций, необходимых по усмотрению участников к привлечению в ходе выполнения задания.</p>
 
<p> 
  <br />
 </p>
 
<p>Участники вправе обращаться за дополнительными разъяснениями по содержанию задания на этап к судьям во время его выполнения, в том числе, могут уточнить, где именно в программе &laquo;Модус&raquo; можно выполнить конкретное действие (участник озвучивает, что хочет сделать и для чего). Судьи, при определении, что ответ на вопрос не является подсказкой или указанием к действию, вправе ответить участнику на заданный вопрос.</p>
 
<p> 
  <br />
 </p>
 
<p>Производство оперативных переключений прекращается при завершении выполнения задачи докладом НСС или по истечении установленного контрольного времени (контроль ведется &laquo;Модус&raquo; и судьями). 
  <br />
 </p>
 
<p> 
  <br />
 </p>
 
<p>Оценка выполненного задания и начисление штрафных баллов производится в соответствии с технологической картой этапа и разработанного типового бланка.</p>
 
<p> 
  <br />
 </p>
 
<p>Начисленный штраф за выполнение задания не может превышать максимальной оценки этого задания (максимального количества баллов за этап).</p>
 
<p> 
  <br />
 </p>
 
<p>Излишние операции, не влияющие на порядок переключений, ошибочными не считаются и за их выполнение штрафные или дополнительные баллы не начисляются, кроме операций, приводящих к излишнему изменению режима работы оборудования, не включенного в объем задания.</p>
 
<p> 
  <br />
 </p>
 
<p>Если контрольное время истекло, а задание на производство оперативных переключений не выполнено, то судьи фиксируют окончание контрольного времени, и прекращают работу команды на этапе. За неполное выполнение задания команде дополнительно начисляется 20 штрафных баллов и штрафные баллы за каждую не выполненную операцию в соответствии с протоколом ПЭВМ, технологической картой и типовым бланком. 
  <br />
 </p>
 
<p> 
  <br />
 </p>
 
<p>Если при переключениях допущена одна из грубейших ошибочных операций, а именно: </p>
 
<ul> 
  <li>отключение (включение) разъединителей под нагрузкой; </li>
 
  <li> включение заземляющих разъединителей на участок электрической сети (ошиновка и т.п.), находящийся под напряжением;</li>
 
  <li> подача напряжения на заземленный участок электрической сети (шины, ошиновка и т.п.);</li>
 
  <li> неправильные операции с устройствами РЗА, приведшие к отключению оборудования, тренировка прекращается, команда снимается с этапа, команде по результатам прохождения этапа начисляется 0 баллов. Повторное прохождение командой этапа не допускается.</li>
 </ul>
 
<p> 
  <br />
 </p>
 
<p>Судейская бригада по единогласному решению может присудить команде до 60 дополнительных баллов за нестандартные решения, не противоречащие НТД и упростившие или сократившие время выполнения задания относительно принятого в типовом бланке. Краткое описание причины данного решения указывается в итоговом протоколе. 
  <br />
 </p>
 
<p> 
  <br />
 </p>
 
<p>Судейская бригада так же производит оценку качества ведения оперативных переговоров, при отсутствии замечаний к ведению переговоров итоговая оценка не снижается, а при наличии замечаний суммарное снижение оценки всем участникам за качество переговоров не может быть более 80 баллов. 
  <br />
 </p>
 
<p> 
  <br />
 </p>
 
<p>При отсутствии у судейской бригады доказательств выполнения той или иной операции по заданию (итоговый протокол ПЭВМ, аудиовизуальное протоколирование, осуществляемое на этапе), судейская бригада признает операцию невыполненной. При наличии подтверждений о том, что участники команд выполняли те или иные операции не в соответствии с установленным заданием или командой (во время инструктажа) распределением обязанностей в команде, судейская бригада считает такие операции невыполненными, даже при наличии излишнего дублирующего выполнения (касается и операций по осмотру). 
  <br />
 </p>
 
<p> 
  <br />
 </p>
 
<p>Судейская бригада после окончания тренировки проводит совещание по действиям и оценке участников соревнований, после чего с руководителем команды проводит краткий разбор основных ошибок по тренировке, и составляет протокол с итоговой оценкой.</p>
 
<p> 
  <br />
 </p>
 
<p>Итоговый протокол заполняется судейской бригадой, и с итогами прохождения этапа передается в секретариат соревнований с подписями судей и руководителя команды не позднее 1 часа после окончания времени прохождения этапа. 
  <br />
 </p>
 
<p> 
  <br />
 </p>
 
<p>Форма итогового протокола приведена в Приложении к настоящему Положению.</p>
 
<p> 
  <br />
 </p>
 
<p> </p>
 
<p><b>Контрольное время &ndash; 80 минут, общее время &ndash; 120 минут.</b></p>
 
<p><b> 
    <br />
   </b></p>
 
<p>Общее время распределяется следующим образом:</p>
 
<p>* инструктаж 10 минут;</p>
 
<p>* работа на ПК 80 минут;</p>
 
<p>* оформление документов, оглашение итогов 30 минут;</p>
 
<p> 
  <br />
 </p>
 
<p>ИТОГО: 120 минут.</p>
 
